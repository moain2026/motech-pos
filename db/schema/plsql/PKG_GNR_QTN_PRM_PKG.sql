					       DIS_PER = (((I.QT_QTY / I.I_QTY) * I.DIS_AMT) / J.I_PRICE) * 100,
					       QT_PRM_NO = I.QUOT_NO,
					       QT_PRM_SER = I.QUOT_SER,
					       QT_PRM_RCRD_NO = I.RCRD_NO
					 WHERE I_CODE = J.I_CODE AND RCRD_NO = J.RCRD_NO AND BILL_SRL = P_BILL_SER AND I.I_QTY > 0 AND J.I_PRICE > 0;
				    ELSE
					UPDATE IAS_POS_HUNG_BILLS
					   SET DIS_AMT_DTL = (I.QT_QTY / I.I_QTY) * I.DIS_AMT,
					       DIS_PER = (((I.QT_QTY / I.I_QTY) * I.DIS_AMT) / J.I_PRICE) * 100,
					       QT_PRM_NO = I.QUOT_NO,
					       QT_PRM_SER = I.QUOT_SER,
					       QT_PRM_RCRD_NO = I.RCRD_NO,
					       QT_PRM_METHOD = I.QT_PRM_METHOD,
					       CHNG_FLG = 1
					 WHERE I_CODE = J.I_CODE AND RCRD_NO = J.RCRD_NO AND DOC_D_SEQ = J.DOC_D_SEQ AND I.I_QTY > 0 AND J.I_PRICE > 0;
				    END IF;
				--V_DIS_PER := (I.Dis_Amt/J.I_Price)*100;
				--V_Dis_Amt := I.Dis_Amt;
				END IF;

			    ELSIF I.QT_PRM_METHOD = 7 THEN
				IF NVL(I.GRNT_FREE_QTY_TYP,0) IN (2,3,4) THEN
				    GET_PRM_GRP_ICODE(P_PRM_GRP_NO	    => I.PRM_GRP_NO,
						      P_GRNT_FREE_QTY_TYP   => I.GRNT_FREE_QTY_TYP,
						      P_QT_I_CODE	    => V_QT_I_CODE,
						      P_QT_ITM_UNT	    => V_QT_ITM_UNT);
				ELSE
				    V_QT_I_CODE := I.QT_I_CODE;
				    V_QT_ITM_UNT := I.QT_ITM_UNT;
				END IF;

				BEGIN
				    IF V_CALL_TYP = 2 THEN
					SELECT 1
					  INTO V_CNT
					  FROM IAS_POS_BILL_DTL
					 WHERE BILL_SRL = P_BILL_SER
					   AND I_CODE = V_QT_I_CODE
					   AND ITM_UNT = V_QT_ITM_UNT
					   AND NVL(QT_PRM_NO, 0) = I.QUOT_NO
					   AND NVL(QT_PRM_SER, 0) = I.QUOT_SER
					   AND NVL(QT_PRM_RCRD_NO, 0) = I.RCRD_NO
					   AND ROWNUM <= 1;
				    ELSE
					SELECT 1
					  INTO V_CNT
					  FROM IAS_POS_HUNG_BILLS
					 WHERE I_CODE = V_QT_I_CODE
					   AND ITM_UNT = V_QT_ITM_UNT
					   AND NVL(QT_PRM_NO, 0) = I.QUOT_NO
					   AND NVL(QT_PRM_SER, 0) = I.QUOT_SER
					   AND NVL(QT_PRM_RCRD_NO, 0) = I.RCRD_NO
					   AND ROWNUM <= 1;
				    END IF;

				EXCEPTION
				    WHEN OTHERS THEN
					V_CNT := 0;
				END;

				IF   NVL(V_CNT, 0) = 0 AND (I.QT_QTY * I.FREE_QTY) > 0 AND V_QT_I_CODE IS NOT NULL AND V_QT_ITM_UNT IS NOT NULL /*AND (IAS_ITM_PKG.CHECK_SRV_ITM(P_ICODE => V_QT_I_CODE) = 1
				  OR GET_ICODE_AVLQTY(P_ICODE => V_QT_I_CODE, P_PSIZE => IAS_ITM_PKG.GET_ICODE_SIZE_UNIT(P_I_CODE => V_QT_I_CODE, P_ITM_UNT => V_QT_ITM_UNT), P_WCODE => J.W_CODE) >= (I.QT_QTY * I.FREE_QTY)) */THEN

				    IF V_CALL_TYP = 2 THEN
					ADD_NW_DTL_RCRD(P_I_CODE	    => V_QT_I_CODE,
							P_ITM_UNT	    => V_QT_ITM_UNT,
							P_I_QTY 	    => 0,
							P_FREE_QTY	    => (CASE I.QT_PRM_TYPE WHEN 2 THEN I.FREE_QTY ELSE I.QT_QTY * I.FREE_QTY END),
							P_EXPIRE_DATE	    => NULL,
							P_BATCH_NO	    => NULL,
							P_BARCODE	    => NULL,
							P_QT_I_QTY	    => NULL,
							P_QT_PRM_NO	    => I.QUOT_NO,
							P_QT_PRM_SER	    => I.QUOT_SER,
							P_QT_PRM_RCRD_NO    => I.RCRD_NO ,
							P_PRM_GRP_NO	    => I.PRM_GRP_NO,
							P_QT_PRM_TYPE	    => I.QT_PRM_TYPE,
							P_QT_PRM_METHOD     => I.QT_PRM_METHOD,
							P_DOC_D_SEQ	    => J.DOC_D_SEQ);
				    ELSE
					INSERT INTO IAS_POS_HUNG_BILLS(BILL_SEQ,
								       DOC_D_SEQ,
								       I_CODE,
								       ITM_UNT,
								       P_SIZE,
								       I_QTY,
								       QT_FREE_QTY,
								       I_PRICE,
								       W_CODE,
								       DIS_PER,
								       DIS_AMT_DTL,
								       RCRD_NO,
								       QT_PRM_NO,
								       QT_PRM_SER,
								       QT_PRM_RCRD_NO,
								       QT_PRM_METHOD,
								       PRM_GRP_NO,
								       CHNG_FLG)
					     VALUES (P_DOC_M_SQNC,
						     POS_BILL_DTL_SEQ.NEXTVAL,
						     V_QT_I_CODE,
						     V_QT_ITM_UNT,
						     IAS_ITM_PKG.GET_ICODE_SIZE_UNIT(P_I_CODE => V_QT_I_CODE, P_ITM_UNT => V_QT_ITM_UNT),
						     NULL,
						     DECODE(I.QT_PRM_TYPE, 2, I.FREE_QTY, I.QT_QTY * I.FREE_QTY),
						     NULL,
						     NULL,
						     0,
						     0,
						     V_RCRD_NO,
						     I.QUOT_NO,
						     I.QUOT_SER,
						     I.RCRD_NO,
						     I.QT_PRM_METHOD,
						     I.PRM_GRP_NO,
						     1);
				    END IF;

				ELSE

				    IF V_CALL_TYP = 2 THEN
					UPDATE IAS_POS_BILL_DTL
					   SET FREE_QTY = I.QT_QTY * I.FREE_QTY
					 WHERE I_CODE = V_QT_I_CODE AND ITM_UNT = V_QT_ITM_UNT AND QT_PRM_NO = I.QUOT_NO AND QT_PRM_SER = I.QUOT_SER AND QT_PRM_RCRD_NO = I.RCRD_NO AND BILL_SRL = P_BILL_SER;
				    ELSE
					UPDATE IAS_POS_HUNG_BILLS
					   SET QT_FREE_QTY = I.QT_QTY * I.FREE_QTY, CHNG_FLG = 1,QT_PRM_METHOD = I.QT_PRM_METHOD
					 WHERE I_CODE = V_QT_I_CODE AND ITM_UNT = V_QT_ITM_UNT AND QT_PRM_NO = I.QUOT_NO AND QT_PRM_SER = I.QUOT_SER AND QT_PRM_RCRD_NO = I.RCRD_NO;
					   --AND IAS_GET_QT_PRM_METHOD(I.QUOT_SER) = 7;
				    END IF;

				END IF;
			    END IF;
			END LOOP;
		    END;

		------------------------------------------------------------------------------------------------------------
		END IF;

	    ELSIF I.QT_PRM_TYPE = 2 THEN
		IF I.QT_PRM_METHOD <> 7 THEN
		    IF I.CALC_ALL_SLIDES = 1 AND NVL(I.I_QTY, 0) > 0 THEN

			SELECT SUM(CASE
				       WHEN DISC_TYPE = 1 AND D.T_QTY <= I.I_QTY THEN (((D.T_QTY - LAG_QTY.L_QTY) * (D.DISC_AMT_PER / 100)) / I.I_QTY)
				       WHEN DISC_TYPE = 1 AND D.T_QTY > I.I_QTY THEN (((I.I_QTY - LAG_QTY.L_QTY) * (D.DISC_AMT_PER / 100)) / I.I_QTY)
				   END) * 100,
			       SUM(CASE
				       WHEN DISC_TYPE = 2 AND D.T_QTY <= I.I_QTY THEN (((D.T_QTY - LAG_QTY.L_QTY) * D.DISC_AMT_PER) / I.I_QTY)
				       WHEN DISC_TYPE = 2 AND D.T_QTY > I.I_QTY THEN (((I.I_QTY - LAG_QTY.L_QTY) * D.DISC_AMT_PER) / I.I_QTY)
				   END)
			  INTO V_DIS_PER, V_DIS_AMT
			  FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D, (SELECT QUOT_SER, RCRD_NO, NVL(LAG(T_QTY) OVER(PARTITION BY QUOT_NO ORDER BY T_QTY), 0) L_QTY FROM IAS_QUT_PRM_DTL WHERE QUOT_SER = I.QUOT_SER) LAG_QTY
			 WHERE M.QUOT_SER = D.QUOT_SER
			   AND M.QUOT_SER = I.QUOT_SER
			   AND D.PRM_GRP_NO = I.PRM_GRP_NO
			   AND LAG_QTY.QUOT_SER = M.QUOT_SER
			   AND LAG_QTY.QUOT_SER = D.QUOT_SER
			   AND LAG_QTY.RCRD_NO = D.RCRD_NO
			   AND D.F_QTY <= I.I_QTY;
		    ELSIF I.CALC_ALL_SLIDES = 0 THEN
			BEGIN
			    SELECT DECODE(DISC_TYPE, 1, DISC_AMT_PER), DECODE(DISC_TYPE, 2, NVL(ROUND(DISC_AMT_PER * IAS_GEN_PKG.GET_CUR_RATE(M.QUOT_CUR) / P_BILL_RATE, P_NO_OF_DCML), 0)) --1306 XXX
			      INTO V_DIS_PER, V_DIS_AMT
			      FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D
			     WHERE M.QUOT_SER = D.QUOT_SER AND M.QUOT_SER = I.QUOT_SER AND D.PRM_GRP_NO = I.PRM_GRP_NO AND I.I_QTY BETWEEN D.F_QTY AND D.T_QTY AND ROWNUM <= 1;
			EXCEPTION
			    WHEN OTHERS THEN
				V_DIS_PER := 0;
				V_DIS_AMT := 0;
			END;
		    END IF;
		    IF V_CALL_TYP = 2 THEN DEBUG_PRC('#1881 V_DIS_PER='||V_DIS_PER);
			UPDATE IAS_POS_BILL_DTL A
			   SET DIS_AMT_DTL = (CASE WHEN NVL( V_DIS_PER, 0) > 0 THEN (NVL(I_PRICE, 0) * NVL( V_DIS_PER, 0)) / 100 WHEN NVL(V_DIS_AMT, 0) > 0 AND NVL(I_PRICE, 0) > 0 THEN V_DIS_AMT ELSE 0 END),
			       DIS_PER = (CASE WHEN NVL( V_DIS_PER, 0) > 0 THEN V_DIS_PER WHEN NVL(V_DIS_AMT, 0) > 0 AND NVL(I_PRICE, 0) > 0 THEN (V_DIS_AMT / I_PRICE) * 100 ELSE 0 END),
			       QT_PRM_NO = I.QUOT_NO,
			       QT_PRM_SER = I.QUOT_SER,
			       QT_PRM_RCRD_NO = I.RCRD_NO
			 WHERE BILL_SRL = P_BILL_SER
			   AND EXISTS
				   (SELECT 1
				      FROM IAS_QUT_PRM_GRP_DTL
				     WHERE I_CODE = A.I_CODE AND ITM_UNT = A.ITM_UNT AND PRM_GRP_NO = I.PRM_GRP_NO AND ROWNUM <= 1);
		    ELSE
			UPDATE IAS_POS_HUNG_BILLS A
			   SET DIS_AMT_DTL = (CASE WHEN NVL( V_DIS_PER, 0) > 0 THEN (NVL(I_PRICE, 0) * NVL( V_DIS_PER, 0)) / 100 WHEN NVL(V_DIS_AMT, 0) > 0 AND NVL(I_PRICE, 0) > 0 THEN V_DIS_AMT ELSE 0 END),
			       DIS_PER = (CASE WHEN NVL( V_DIS_PER, 0) > 0 THEN V_DIS_PER WHEN NVL(V_DIS_AMT, 0) > 0 AND NVL(I_PRICE, 0) > 0 THEN (V_DIS_AMT / I_PRICE) * 100 ELSE 0 END),
			       QT_PRM_NO = I.QUOT_NO,
			       QT_PRM_SER = I.QUOT_SER,
			       QT_PRM_RCRD_NO = I.RCRD_NO,
			       QT_PRM_METHOD = I.QT_PRM_METHOD,
			       CHNG_FLG = 1
			 WHERE EXISTS
				   (SELECT 1
				      FROM IAS_QUT_PRM_GRP_DTL
				     WHERE I_CODE = A.I_CODE AND ITM_UNT = A.ITM_UNT AND PRM_GRP_NO = I.PRM_GRP_NO AND ROWNUM <= 1);
		    END IF;

		ELSIF I.QT_PRM_METHOD = 7 THEN
		    BEGIN
			SELECT TRUNC(
				   SUM(
				       CASE
					   WHEN D.T_QTY <= I.I_QTY AND I.CALC_ALL_SLIDES = 1 THEN
					       TRUNC((T_QTY - F_QTY + 1) / DECODE(I.BY_COMP_QTY, 1, COMP_QTY, (T_QTY - F_QTY + 1))) * FREE_QTY
					   WHEN D.T_QTY > I.I_QTY AND I.CALC_ALL_SLIDES = 1 THEN
					       TRUNC((I.I_QTY - F_QTY + 1) / DECODE(I.BY_COMP_QTY, 1, COMP_QTY, (T_QTY - F_QTY + 1))) * FREE_QTY
					   WHEN I.CALC_ALL_SLIDES = 0 AND (I.I_QTY BETWEEN D.F_QTY AND D.T_QTY) AND I.CALC_ALL_SLIDES = 0 THEN
					       TRUNC(TRUNC(I.I_QTY / DECODE(I.BY_COMP_QTY, 1, NVL(COMP_QTY, 1), I.I_QTY)) * FREE_QTY)
				       END))
			  INTO V_FREE_QTY
			  FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D
			 WHERE M.QUOT_SER = D.QUOT_SER
			   AND M.QUOT_SER = I.QUOT_SER
			   AND M.QT_PRM_METHOD = 7
			   AND D.PRM_GRP_NO = I.PRM_GRP_NO
			   AND D.F_QTY <= I.I_QTY
			   AND DECODE(NVL(M.QT_PRM_WC_TYPE, 0),  1, D.W_CODE,  2, D.WHG_CODE,  3, D.CNTRY_NO,  4, D.PROV_NO,  5, D.CITY_NO,  6, D.R_CODE,  0) = DECODE(NVL(M.QT_PRM_WC_TYPE, 0), 0, 0, I.W_CODE);
		    EXCEPTION
			WHEN OTHERS THEN
			    V_FREE_QTY := 0;
		    END;
		    IF NVL(I.GRNT_FREE_QTY_TYP,0) IN (2,3,4) THEN
			GET_PRM_GRP_ICODE(P_PRM_GRP_NO		=> I.PRM_GRP_NO,
					  P_GRNT_FREE_QTY_TYP	=> I.GRNT_FREE_QTY_TYP,
					  P_QT_I_CODE		=> V_QT_I_CODE,
					  P_QT_ITM_UNT		=> V_QT_ITM_UNT);
		    ELSE
			V_QT_I_CODE := I.QT_I_CODE;
			V_QT_ITM_UNT := I.QT_ITM_UNT;
		    END IF;

		    BEGIN
			SELECT W_CODE
			  INTO V_W_CODE
			  FROM IAS_POS_HUNG_BILLS
			 WHERE W_CODE IS NOT NULL AND ROWNUM <= 1;
		    EXCEPTION WHEN OTHERS THEN
			 V_CNT := 0;
		    END;
		    BEGIN
			SELECT 1
			  INTO V_CNT
			  FROM IAS_POS_HUNG_BILLS
			 WHERE I_CODE = V_QT_I_CODE
			   AND ITM_UNT = V_QT_ITM_UNT
			   AND NVL(QT_PRM_NO, 0) = I.QUOT_NO
			   AND NVL(QT_PRM_SER, 0) = I.QUOT_SER
			   AND NVL(PRM_GRP_NO, 0) = I.PRM_GRP_NO
			   --AND IAS_GET_QT_PRM_METHOD(NVL(I.QUOT_SER, 0)) = 7
			   AND ROWNUM <= 1;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_CNT := 0;
		    END;

		    IF NVL(V_CNT, 0) = 0 AND NVL(V_FREE_QTY, 0) > 0 AND V_QT_I_CODE IS NOT NULL AND V_QT_ITM_UNT IS NOT NULL /*AND (IAS_ITM_PKG.CHECK_SRV_ITM(P_ICODE => V_QT_I_CODE) = 1
		     OR GET_ICODE_AVLQTY(P_ICODE => V_QT_I_CODE, P_PSIZE => IAS_ITM_PKG.GET_ICODE_SIZE_UNIT(P_I_CODE => V_QT_I_CODE, P_ITM_UNT => V_QT_ITM_UNT), P_WCODE => V_W_CODE) >= NVL(I.QT_QTY * I.FREE_QTY, 0))*/ THEN

			IF V_CALL_TYP = 2 THEN
			    ADD_NW_DTL_RCRD(P_I_CODE		=> V_QT_I_CODE,
					    P_ITM_UNT		=> V_QT_ITM_UNT,
					    P_I_QTY		=> 0,
					    P_FREE_QTY		=> V_FREE_QTY,
					    P_EXPIRE_DATE	=> NULL,
					    P_BATCH_NO		=> NULL,
					    P_BARCODE		=> NULL,
					    P_QT_I_QTY		=> NULL,
					    P_QT_PRM_NO 	=> I.QUOT_NO,
					    P_QT_PRM_SER	=> I.QUOT_SER,
					    P_QT_PRM_RCRD_NO	=> I.RCRD_NO ,
					    P_PRM_GRP_NO	=> I.PRM_GRP_NO,
					    P_QT_PRM_TYPE	=> I.QT_PRM_TYPE,
					    P_QT_PRM_METHOD	=> I.QT_PRM_METHOD,
					    P_DOC_D_SEQ 	=> I.DOC_D_SEQ);
			ELSE
			    INSERT INTO IAS_POS_HUNG_BILLS(BILL_SEQ,
							   DOC_D_SEQ,
							   I_CODE,
							   ITM_UNT,
							   P_SIZE,
							   I_QTY,
							   QT_FREE_QTY,
							   I_PRICE,
							   W_CODE,
							   DIS_PER,
							   DIS_AMT_DTL,
							   RCRD_NO,
							   QT_PRM_NO,
							   QT_PRM_SER,
							   QT_PRM_RCRD_NO,
							   QT_PRM_METHOD,
							   PRM_GRP_NO,
							   CHNG_FLG)
				 VALUES (P_DOC_M_SQNC,
					 POS_BILL_DTL_SEQ.NEXTVAL,
					 V_QT_I_CODE,
					 V_QT_ITM_UNT,
					 IAS_ITM_PKG.GET_ICODE_SIZE_UNIT(P_I_CODE => V_QT_I_CODE, P_ITM_UNT => V_QT_ITM_UNT),
					 NULL,
					 V_FREE_QTY,
					 NULL,
					 NULL,
					 0,
					 0,
					 V_RCRD_NO,
					 I.QUOT_NO,
					 I.QUOT_SER,
					 I.RCRD_NO,
					 I.QT_PRM_METHOD,
					 I.PRM_GRP_NO,
					 1);
			END IF;
		    ELSE


			IF V_CALL_TYP = 2 THEN
			    UPDATE IAS_POS_BILL_DTL
			       SET FREE_QTY = V_FREE_QTY
			     WHERE BILL_SRL = P_BILL_SER
			       AND I_CODE = V_QT_I_CODE AND ITM_UNT = V_QT_ITM_UNT AND QT_PRM_NO = I.QUOT_NO AND QT_PRM_SER = I.QUOT_SER AND PRM_GRP_NO = I.PRM_GRP_NO;
			ELSE
			    UPDATE IAS_POS_HUNG_BILLS
			       SET QT_FREE_QTY = V_FREE_QTY, CHNG_FLG = 1,QT_PRM_METHOD = I.QT_PRM_METHOD
			     WHERE I_CODE = V_QT_I_CODE AND ITM_UNT = V_QT_ITM_UNT AND QT_PRM_NO = I.QUOT_NO AND QT_PRM_SER = I.QUOT_SER AND PRM_GRP_NO = I.PRM_GRP_NO;
			     --AND IAS_GET_QT_PRM_METHOD(I.QUOT_SER) = 7;
			END IF;

		    END IF;
		END IF;

	    END IF;
	END LOOP;
    EXCEPTION
	WHEN OTHERS THEN
	    DEBUG_PRC('#######EXCEPTION'||SQLERRM);
    END IAS_CLC_QTN_PRM_GRP_PRC;

    FUNCTION GET_QT_PRICE_FNC(P_ICODE		IN VARCHAR2, -- ITEM_CODE
			      P_ITM_UNT 	IN VARCHAR2 DEFAULT NULL, -- ITEM_UNIT
			      P_BATCH_NO	IN VARCHAR2 DEFAULT NULL, -- BATCH_NO
			      P_EXP_DATE	IN DATE DEFAULT NULL, -- EXPAIRE_DATE
			      P_BILL_DOC_TYPE	IN NUMBER DEFAULT NULL, -- PAYMENT_TYPE
			      P_ACY		IN VARCHAR2, -- INVOICE CURRENCY
			      P_BILL_RATE	IN NUMBER, -- CURRENCY_RATE
			      P_DATE		IN DATE DEFAULT NULL, -- INVOICE DATE
			      P_NO_OF_DCML	IN NUMBER DEFAULT 2, -- NUMBER OF DECIMAL
			      P_LEV_NO		IN NUMBER, -- PRICE LEVEL NO
			      P_WCODE		IN NUMBER DEFAULT NULL, -- WAREHOUSE NO 	   -- IF PRICING BY W_CODE
			      P_IQTY		IN NUMBER DEFAULT NULL, -- ITEM QUANTITY	   -- IF PRICING BY QUANTITY
			      P_SYS_TYP 	IN NUMBER, --1- ONYX PRO 2- POS SYSTME 3- DTS SYSTEM 4- SHO SYSTEM
			      P_BRN_NO		IN NUMBER DEFAULT NULL)
	RETURN NUMBER
    IS --BRANCH NO -- IF PRICING BY BRANCH
	V_QT_ICODE     IAS_QUT_PRM_DTL.I_CODE%TYPE;
	V_PRICE        IAS_QUT_PRM_DTL.I_PRICE%TYPE;
	V_ITM_UNT      IAS_ITM_UNT_BARCODE.ITM_UNT%TYPE;
	V_EXP_DATE     IAS_ITM_UNT_BARCODE.EXPIRE_DATE%TYPE;
	V_BATCH_NO     IAS_ITM_UNT_BARCODE.BATCH_NO%TYPE;
	V_LOC_CUR      EX_RATE.CUR_CODE%TYPE;
	V_STK_CUR      EX_RATE.CUR_CODE%TYPE;
	V_STK_RATE     NUMBER;
	V_PRICE_TYPE   NUMBER;
    BEGIN

	------------------------------------------------------------
	BEGIN

	    SELECT I_CODE,
		   ITM_UNT,
		   EXPIRE_DATE,
		   BATCH_NO
	      INTO V_QT_ICODE,
		   V_ITM_UNT,
		   V_EXP_DATE,
		   V_BATCH_NO
	      FROM IAS_ITM_UNT_BARCODE
	     WHERE BARCODE = P_ICODE AND ROWNUM <= 1;
	EXCEPTION
	    WHEN OTHERS THEN
		NULL;
	END;

	------------------------------------------------------------
	IF V_QT_ICODE IS NULL THEN

	    BEGIN

		SELECT I_CODE
		  INTO V_QT_ICODE
		  FROM IAS_ITM_DTL
		 WHERE IAS_ITM_DTL.SALE_UNIT = 1 AND IAS_ITM_DTL.BARCODE = P_ICODE AND ROWNUM <= 1;
	    EXCEPTION
		WHEN OTHERS THEN
		    V_QT_ICODE := P_ICODE;
		    V_ITM_UNT := P_ITM_UNT;
	    END;

	END IF;

	------------------------------------------------------------
	BEGIN

	    SELECT (NVL(IAS_QUT_PRM_DTL.I_PRICE, 0) * NVL(ROUND(IAS_GEN_PKG.GET_CUR_RATE(IAS_QUT_PRM_MST.QUOT_CUR) / P_BILL_RATE, P_NO_OF_DCML), 0)) V_PRICE
	      INTO V_PRICE
	      FROM IAS_QUT_PRM_MST, IAS_QUT_PRM_DTL
	     WHERE IAS_QUT_PRM_MST.QUOT_SER = IAS_QUT_PRM_DTL.QUOT_SER
	       AND IAS_QUT_PRM_MST.QT_PRM_TYPE = 1
	       AND IAS_QUT_PRM_MST.QT_PRM_METHOD = 1
	       AND IAS_QUT_PRM_DTL.I_CODE = V_QT_ICODE
	       AND IAS_QUT_PRM_DTL.ITM_UNT = V_ITM_UNT
	       AND NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0') = DECODE(NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0'), '0', NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0'), NVL(P_BATCH_NO, '0'))
	       AND NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900') = DECODE(NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900'), '01/01/1900', NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900'), NVL(P_EXP_DATE, '01/01/1900'))
	       AND NVL(IAS_QUT_PRM_MST.INACTIVE, 0) = 0
	       AND DECODE(P_BILL_DOC_TYPE,  4, 2,  8, 2,  1) = DECODE(IAS_QUT_PRM_MST.BILL_DOC_TYPE, NULL, DECODE(P_BILL_DOC_TYPE,  4, 2,  8, 2,  1), IAS_QUT_PRM_MST.BILL_DOC_TYPE)
	       AND TO_CHAR(NVL(P_DATE, IAS_GEN_PKG.GET_SYSDATE), 'DD/MM/YYYY') BETWEEN F_DATE AND T_DATE
	       AND TO_CHAR(TO_DATE(TO_CHAR(IAS_GEN_PKG.GET_SYSDATE, 'DD/MM/YYYY')), 'D') IN (FLD_DAY1, FLD_DAY2, FLD_DAY3, FLD_DAY4, FLD_DAY5, FLD_DAY6, FLD_DAY7)
	       AND DECODE(NVL(P_SYS_TYP, 1),  1, NVL(USE_QTN_PRM_IN_IAS_SYS_FLG, 0),  2, NVL(USE_QTN_PRM_IN_POS_SYS_FLG, 0),  3, NVL(USE_QTN_PRM_IN_DTS_SYS_FLG, 0),  4, NVL(USE_QTN_PRM_IN_SHO_SYS_FLG, 0)) = 1
	       AND TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS') >= NVL(IAS_QUT_PRM_MST.F_TIME, TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS'))
	       AND TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS') <= NVL(IAS_QUT_PRM_MST.T_TIME, TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS'))
	       AND ROWNUM <= 1;
	EXCEPTION
	    WHEN OTHERS THEN
		V_PRICE := NULL;
	END;

	------------------------------------------------------------
	IF V_PRICE IS NULL THEN
	    V_LOC_CUR := IAS_GEN_PKG.GET_LOCAL_CUR;
	    V_STK_CUR := IAS_GEN_PKG.GET_STK_CUR;
	    V_STK_RATE := IAS_GEN_PKG.GET_CUR_RATE(V_STK_CUR);

	    BEGIN

		SELECT PRICE_TYPE
		  INTO V_PRICE_TYPE
		  FROM IAS_PARA_INV
		 WHERE ROWNUM <= 1;
	    EXCEPTION
		WHEN OTHERS THEN
		    V_PRICE_TYPE := 1;
	    END;

	    SELECT SUM(DECODE(A_CY,  P_ACY, I_PRICE,  V_STK_CUR, ((I_PRICE * V_STK_RATE) / P_BILL_RATE),  V_LOC_CUR, (I_PRICE / P_BILL_RATE),  ((I_PRICE * CUR_RATE) / P_BILL_RATE)))
	      INTO V_PRICE
	      FROM IAS_PRICING_LEVELS, IAS_ITEM_PRICE, EX_RATE
	     WHERE IAS_PRICING_LEVELS.LEV_NO = IAS_ITEM_PRICE.LEV_NO
	       AND IAS_ITEM_PRICE.LEV_NO = P_LEV_NO
	       AND I_CODE = P_ICODE
	       AND IAS_ITEM_PRICE.ITM_UNT = P_ITM_UNT
	       AND CUR_CODE = A_CY
	       AND DECODE(V_PRICE_TYPE,  2, IAS_ITEM_PRICE.W_CODE,  5, IAS_ITEM_PRICE.BRN_NO,  0) = DECODE(V_PRICE_TYPE,  2, P_WCODE,  5, P_BRN_NO,  0)
	       AND NVL(IAS_PRICING_LEVELS.BILL_DOC_TYPE, 0) = DECODE(IAS_PRICING_LEVELS.BILL_DOC_TYPE, NULL, 0, DECODE(P_BILL_DOC_TYPE,  4, 2,	8, 2,  1))
	       AND DECODE(V_PRICE_TYPE,  3, NVL(P_IQTY, FROM_QTY),  4, NVL(P_IQTY, FROM_QTY),  0) BETWEEN DECODE(V_PRICE_TYPE,	3, NVL(FROM_QTY, 0),  4, NVL(FROM_QTY, 0),  0)
												      AND DECODE(V_PRICE_TYPE,	3, NVL(TO_QTY, 0),  4, NVL(TO_QTY, 0),	0)
	       AND ROWNUM <= 1;

	END IF;
	---------------------------------------------------------------------------------------------
	RETURN (V_PRICE);
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN (0);
    END GET_QT_PRICE_FNC;

    PROCEDURE GET_PRM_GRP_ICODE(P_PRM_GRP_NO	      IN     IAS_QUT_PRM_GRP_MST.PRM_GRP_NO%TYPE,
				P_GRNT_FREE_QTY_TYP   IN     IAS_QUT_PRM_GRP_MST.GRNT_FREE_QTY_TYP%TYPE,
				P_QT_I_CODE	      IN OUT IAS_QUT_PRM_DTL.QT_I_CODE%TYPE,
				P_QT_ITM_UNT	      IN OUT IAS_QUT_PRM_DTL.QT_ITM_UNT%TYPE)
    IS
    BEGIN
	IF P_GRNT_FREE_QTY_TYP = 2 THEN

	    SELECT I_CODE, ITM_UNT
	      INTO P_QT_I_CODE, P_QT_ITM_UNT
	      FROM (  SELECT B.I_CODE, B.ITM_UNT, SUM(NVL(B.I_QTY, 0) * NVL(B.I_PRICE, 0)) TOTAL
			FROM IAS_POS_HUNG_BILLS B, IAS_QUT_PRM_GRP_DTL G
		       WHERE B.I_CODE = G.I_CODE AND G.PRM_GRP_NO = P_PRM_GRP_NO
		    GROUP BY B.I_CODE,
			     B.ITM_UNT
		    ORDER BY TOTAL DESC)
	     WHERE ROWNUM <= 1;

	ELSIF P_GRNT_FREE_QTY_TYP = 3 THEN

	    SELECT I_CODE, ITM_UNT
	      INTO P_QT_I_CODE, P_QT_ITM_UNT
	      FROM (  SELECT B.I_CODE, B.ITM_UNT, SUM(NVL(B.I_QTY, 0) * NVL(B.I_PRICE, 0)) TOTAL
			FROM IAS_POS_HUNG_BILLS B, IAS_QUT_PRM_GRP_DTL G
		       WHERE B.I_CODE = G.I_CODE AND G.PRM_GRP_NO = P_PRM_GRP_NO
		    GROUP BY B.I_CODE,
			     B.ITM_UNT
		    ORDER BY TOTAL ASC)
	     WHERE ROWNUM <= 1;

	ELSIF P_GRNT_FREE_QTY_TYP = 4 THEN

	    SELECT I_CODE, ITM_UNT
	      INTO P_QT_I_CODE, P_QT_ITM_UNT
	      FROM (  SELECT B.I_CODE, B.ITM_UNT
			FROM IAS_POS_HUNG_BILLS B, IAS_QUT_PRM_GRP_DTL G
		       WHERE B.I_CODE = G.I_CODE AND G.PRM_GRP_NO = P_PRM_GRP_NO
		    ORDER BY B.RCRD_NO ASC)
	     WHERE ROWNUM <= 1;

	END IF;
    EXCEPTION
	WHEN OTHERS THEN
	    NULL;
    END GET_PRM_GRP_ICODE;

