	       V_WHG_CODE,
	       V_CNTRY_NO,
	       V_PROV_NO,
	       V_CITY_NO,
	       V_R_CODE,
	       V_LEV_NO,
		       V_BY_LEV_NO
	  FROM (SELECT IAS_QUT_PRM_MST.QUOT_NO,
		       IAS_QUT_PRM_MST.QUOT_SER,
		       IAS_QUT_PRM_MST.QT_PRM_TYPE,
		       IAS_QUT_PRM_MST.QT_PRM_METHOD,
		       IAS_QUT_PRM_MST.QT_PRM_ITM_TYPE,
