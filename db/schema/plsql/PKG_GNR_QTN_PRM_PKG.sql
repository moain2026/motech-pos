	PROCEDURE GET_ITM_DSCNT_QT_PRM(P_DIS_PER IN OUT NUMBER, P_DIS_AMT IN OUT NUMBER)
	IS
	    L_PRICE NUMBER := 0;
	    L_G_CODE VARCHAR2(30);
	BEGIN
	    IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
		L_PRICE := NVL(L_I_PRICE_VAT, 0);
	    ELSE
		L_PRICE := NVL(L_I_PRICE, 0);
	    END IF;

	    IF G_SHOW_DIS_PER_ITEM = 1 AND G_HIDE_DISC_PER = 0 THEN
		IF G_SALES_DISC_TYPE = 0 THEN
		    P_DIS_PER := L_DIS_PER;
		    P_DIS_AMT := (NVL( L_I_PRICE, 0) * NVL(P_DIS_PER, 0)) / 100;
		ELSE

		    BEGIN
			SELECT G_CODE
			  INTO L_G_CODE
			  FROM IAS_ITM_MST
			 WHERE I_CODE = L_I_CODE;
		    EXCEPTION
			WHEN OTHERS THEN
			    L_G_CODE := NULL;
		    END;

		    IAS_GET_SALESDISC_PRC(P_DISCTYPE	       => G_SALES_DISC_TYPE,
					  P_SALES_DISC_WCODE   => G_SALES_DISC_WCODE,
					  P_IPRICE	       => L_I_PRICE,
					  P_BILL_DOC_TYPE      => G_BILL_DOC_TYPE,
					  P_BILL_I_QTY	       => L_I_QTY,
					  P_ICODE	       => L_I_CODE,
					  P_ITM_UNT	       => L_ITM_UNT,
					  P_GCODE	       => L_G_CODE,
					  P_BILLDATE	       => G_BILL_DATE,
					  P_CST_CLASS	       => L_C_CLASS,
					  P_C_CODE	       => L_C_CODE,
					  P_LEVNO	       => G_PRICE_LVL,
					  P_BATCH_NO	       => L_BATCH_NO,
