    FUNCTION GET_QUOT_PRICE_FNC(P_BARCODE IN IAS_ITM_UNT_BARCODE.BARCODE%TYPE, P_NO_OF_DCML IN NUMBER DEFAULT 1)
	RETURN NUMBER
    IS
	V_QT_ICODE   IAS_QUT_PRM_DTL.I_CODE%TYPE;
	V_PRICE      IAS_QUT_PRM_DTL.I_PRICE%TYPE;
	V_QUOT_CUR   IAS_QUT_PRM_MST.QUOT_CUR%TYPE;
	V_ITM_UNT    IAS_ITM_UNT_BARCODE.ITM_UNT%TYPE;
	V_EXP_DATE   IAS_ITM_UNT_BARCODE.EXPIRE_DATE%TYPE;
	V_BATCH_NO   IAS_ITM_UNT_BARCODE.BATCH_NO%TYPE;
    BEGIN

	BEGIN

	    SELECT I_CODE,
		   ITM_UNT,
		   EXPIRE_DATE,
		   BATCH_NO
	      INTO V_QT_ICODE,
		   V_ITM_UNT,
		   V_EXP_DATE,
		   V_BATCH_NO
	      FROM IAS_ITM_UNT_BARCODE
	     WHERE BARCODE = P_BARCODE AND ROWNUM <= 1;
	EXCEPTION
	    WHEN OTHERS THEN
		NULL;
	END;

	IF V_QT_ICODE IS NULL THEN

	    ------------------------------------------------------------
	    BEGIN

		SELECT I_CODE
		  INTO V_QT_ICODE
		  FROM IAS_ITM_DTL
		 WHERE IAS_ITM_DTL.SALE_UNIT = 1 AND IAS_ITM_DTL.BARCODE = P_BARCODE AND ROWNUM <= 1;
	    EXCEPTION
		WHEN OTHERS THEN
		    NULL;
	    END;

	END IF;

	------------------------------------------------------------
	SELECT (NVL(IAS_QUT_PRM_DTL.I_PRICE, 0) * NVL(ROUND(IAS_GEN_PKG.GET_CUR_RATE(IAS_QUT_PRM_MST.QUOT_CUR) / IAS_GEN_PKG.GET_CUR_RATE(P_ACY => IAS_QUT_PRM_MST.QUOT_CUR), P_NO_OF_DCML), 0)) V_PRICE
	  INTO V_PRICE
	  FROM IAS_QUT_PRM_MST, IAS_QUT_PRM_DTL
	 WHERE IAS_QUT_PRM_MST.QUOT_SER = IAS_QUT_PRM_DTL.QUOT_SER
	   AND IAS_QUT_PRM_MST.QT_PRM_TYPE = 1
	   AND (CASE WHEN IAS_QUT_PRM_MST.QT_PRM_TYPE = 1 AND IAS_QUT_PRM_DTL.I_CODE = V_QT_ICODE AND IAS_QUT_PRM_DTL.ITM_UNT = V_ITM_UNT THEN 1 ELSE 0 END) = 1
	   AND NVL(IAS_QUT_PRM_MST.INACTIVE, 0) = 0
	   AND BILL_DOC_TYPE = 1
	   AND TO_CHAR(IAS_GEN_PKG.GET_SYSDATE, 'DD/MM/YYYY') BETWEEN F_DATE AND T_DATE
	   AND TO_CHAR(TO_DATE(TO_CHAR(IAS_GEN_PKG.GET_SYSDATE, 'DD/MM/YYYY')), 'D') IN (FLD_DAY1, FLD_DAY2, FLD_DAY3, FLD_DAY4, FLD_DAY5, FLD_DAY6, FLD_DAY7)
	   And (CASE WHEN IAS_QUT_PRM_MST.F_Time IS NULL  OR IAS_QUT_PRM_MST.F_Time IS NULL THEN 1
								  WHEN IAS_QUT_PRM_MST.F_Time <=IAS_QUT_PRM_MST.T_Time	AND To_Char(Ias_gen_Pkg.Get_Curdate,'HH24:MI:SS') BETWEEN IAS_QUT_PRM_MST.F_Time AND IAS_QUT_PRM_MST.T_Time THEN 1
								  WHEN IAS_QUT_PRM_MST.F_Time > IAS_QUT_PRM_MST.T_Time	AND (To_Char(Ias_gen_Pkg.Get_Curdate,'HH24:MI:SS') > IAS_QUT_PRM_MST.F_Time OR To_Char(Ias_gen_Pkg.Get_Curdate,'HH24:MI:SS') < IAS_QUT_PRM_MST.T_Time ) THEN 1
								  ELSE 0
							      End)=1
	   AND ROWNUM <= 1;

	---------------------------------------------------------------------------------------------
	RETURN (V_PRICE);
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN (0);
    END GET_QUOT_PRICE_FNC;

    PROCEDURE IAS_CLC_QTN_PRM_GRP_PRC(P_DATE		IN DATE,
				      P_BILL_SER	IN NUMBER DEFAULT NULL,
				      P_BILL_DOC_TYPE	IN NUMBER,
				      P_C_CODE		IN VARCHAR2,
				      P_C_GROUP_CODE	IN NUMBER,
				      P_C_CLASS 	IN NUMBER,
				      P_C_DEGREE	IN NUMBER,
				      P_C_CODE_CSH	IN VARCHAR2 DEFAULT NULL,
				      P_DOC_M_SQNC	IN VARCHAR2 DEFAULT NULL,
				      P_CST_GRP_CSH	IN NUMBER,
				      P_CHK_QT_NO	IN NUMBER DEFAULT NULL,
				      P_CHK_QT_SER	IN NUMBER DEFAULT NULL,
				      P_BILL_AMT	IN NUMBER,
				      P_SYS_TYP 	IN NUMBER, --1- ONYX PRO 2- POS SYSTME 3- DTS SYSTEM 4- SHO SYSTEM
				      P_NO_OF_DCML	IN NUMBER,
				      P_BILL_RATE	IN NUMBER DEFAULT 1,
				      P_CALL_TYP	IN NUMBER DEFAULT 1)--# 1- CALC BEFORE SAVE 2-CALC AFTER SAVE
    IS

	V_DIS_PER    NUMBER := 0;
	V_DIS_AMT      NUMBER := 0;
	V_FREE_QTY     NUMBER := 0;
	V_CNT	       NUMBER := 0;
	V_RCRD_NO      NUMBER := 0;
	V_QT_I_CODE    IAS_ITM_MST.I_CODE%TYPE;
	V_QT_ITM_UNT   IAS_ITM_DTL.ITM_UNT%TYPE;
	V_W_CODE       NUMBER;
	V_MIN_QTY      NUMBER;
	V_QTY_IC       NUMBER;
	V_LOOP_INDX    NUMBER := 0;
	V_CALL_TYP     NUMBER := NVL(P_CALL_TYP,1);
    BEGIN

	FOR I IN (SELECT DISTINCT
			 M.QUOT_NO,
			 M.QUOT_SER,
			 M.QT_PRM_TYPE,
			 M.QT_PRM_METHOD,
			 M.QT_PRM_WC_TYPE,
			 M.CALC_ALL_SLIDES,
			 D.QT_I_CODE,
			 D.QT_ITM_UNT,
			 D.RCRD_NO,
			 D.FREE_QTY,
			 NVL(D.COMP_QTY,1) COMP_QTY,
			 GD.PRM_GRP_NO,
			 GM.GRNT_FREE_QTY_TYP,
			 M.BY_COMP_QTY,
			 MAX(BD.DOC_D_SEQ) DOC_D_SEQ,
			 NVL(GM.CHK_ALL_ITMS, 0) CHK_ALL_ITMS,
			 DECODE(M.QT_PRM_TYPE, 1, 0, D.F_QTY) F_QTY,
			 DECODE(M.QT_PRM_TYPE, 1, 0, D.T_QTY) T_QTY,
			 DECODE(M.QT_PRM_WC_TYPE,  1, D.W_CODE,  2, D.WHG_CODE,  3, D.CNTRY_NO,  4, D.PROV_NO,	5, D.CITY_NO,  6, D.R_CODE) W_CODE,
			 SUM(NVL(BD.I_QTY,0) * BD.P_SIZE) NET_I_QTY,
			 SUM(NVL(BD.I_QTY,0) * BD.P_SIZE * GREATEST(NVL(BD.I_PRICE,0),NVL(BD.I_PRICE_VAT,0))) NET_I_PRICE,
			 TRUNC(SUM((NVL(BD.I_QTY, 0) * NVL(BD.P_SIZE, 0)) / NVL(GD.P_SIZE, 0))) I_QTY,
			 TRUNC(SUM(((NVL(BD.I_QTY, 0) * NVL(BD.P_SIZE, 0)) / NVL(GD.P_SIZE, 0))) / NVL(D.COMP_QTY,SUM((NVL(BD.I_QTY, 0) * NVL(BD.P_SIZE, 0)) / NVL(GD.P_SIZE, 0)))) QT_QTY,
			 (CASE WHEN M.QT_PRM_METHOD = DECODE(M.QT_PRM_TYPE, 1, 6) AND D.DISC_TYPE = 1 THEN D.DISC_AMT_PER ELSE NULL END) DIS_PER,
			 (CASE WHEN M.QT_PRM_METHOD = DECODE(M.QT_PRM_TYPE, 1, 6) AND D.DISC_TYPE = 2 THEN NVL(ROUND(D.DISC_AMT_PER * IAS_GEN_PKG.GET_CUR_RATE(M.QUOT_CUR) / P_BILL_RATE, P_NO_OF_DCML), 0) ELSE NULL END) DIS_AMT
		    FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D, IAS_POS_HUNG_BILLS BD, IAS_QUT_PRM_GRP_MST GM, IAS_QUT_PRM_GRP_DTL GD
		   WHERE M.QUOT_SER = D.QUOT_SER
		     AND GM.PRM_GRP_NO = GD.PRM_GRP_NO
		     AND M.QUOT_SER = NVL(P_CHK_QT_SER, M.QUOT_SER)
		     AND M.QT_PRM_TYPE IN (1, 2)
		     AND M.QT_PRM_METHOD IN (6, 7)
		     AND BD.I_CODE = GD.I_CODE
		     AND BD.ITM_UNT = GD.ITM_UNT
		     AND D.PRM_GRP_NO = GD.PRM_GRP_NO
		     AND (CASE
			      WHEN M.QT_PRM_CST_TYPE = 0 THEN 1
