								  WHEN M.F_Time > M.T_Time  AND (To_Char(Ias_gen_Pkg.Get_Curdate,'HH24:MI:SS') > M.F_Time OR To_Char(Ias_gen_Pkg.Get_Curdate,'HH24:MI:SS') < M.T_Time ) THEN 1
