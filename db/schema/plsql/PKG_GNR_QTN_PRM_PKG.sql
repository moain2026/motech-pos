		       (CASE WHEN IAS_QUT_PRM_MST.QT_PRM_METHOD = DECODE(IAS_QUT_PRM_MST.QT_PRM_TYPE, 3, 1, 2) AND DISC_TYPE = 1 THEN IAS_QUT_PRM_DTL.DISC_AMT_PER ELSE NULL END) DIS_PER,
