    FUNCTION CHK_QT_PRM_EXCPT(P_C_CODE VARCHAR2)
	RETURN BOOLEAN
    IS
    BEGIN
	IF P_C_CODE IS NOT NULL THEN
	    FOR CHK IN (SELECT 1
			  FROM CUSTOMER
			 WHERE C_CODE = P_C_CODE AND NVL(CST_EXCPT_QT_PRM_FLG, 0) = 1) LOOP
		RETURN TRUE;
	    END LOOP;
	ELSE
	    RETURN FALSE;
	END IF;

	RETURN FALSE;
    EXCEPTION
	WHEN NO_DATA_FOUND THEN
	    RETURN FALSE;
	WHEN OTHERS THEN
	    RETURN TRUE;
    END CHK_QT_PRM_EXCPT;

    PROCEDURE IAS_GET_QT_PRM(P_DATE			 IN	DATE,
			     P_BILL_SER 		 IN	NUMBER,
			     P_ICODE			 IN	VARCHAR2,
