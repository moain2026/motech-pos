		    IF TRUE /*	 IAS_ITM_PKG.CHECK_SRV_ITM (P_ICODE => I.QT_I_CODE) = 1
			OR GET_ICODE_AVLQTY_PRIV (P_ICODE => I.QT_I_CODE, P_PSIZE => IAS_ITM_PKG.GET_ICODE_SIZE_UNIT (P_I_CODE => I.QT_I_CODE, P_ITM_UNT => I.QT_ITM_UNT), P_WCODE => L_W_CODE, P_USERNO => NVL (G_USER_NO, 1)) >=
