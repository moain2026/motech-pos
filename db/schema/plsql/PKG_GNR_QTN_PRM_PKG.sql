    FUNCTION CHK_QT_PRM_INACTV(P_QT_SER IN NUMBER, P_DATE IN DATE)
	RETURN NUMBER
    IS
	V_INACTV   NUMBER;
	V_CNT	   NUMBER;
    BEGIN
	IF P_QT_SER IS NOT NULL THEN

	    BEGIN

		SELECT NVL(INACTIVE, 0)
		  INTO V_INACTV
		  FROM IAS_QUT_PRM_MST
		 WHERE QUOT_SER = P_QT_SER AND ROWNUM <= 1;
	    EXCEPTION
		WHEN OTHERS THEN
		    V_INACTV := 0;
	    END;

	    IF NVL(V_INACTV, 0) = 0 THEN

		BEGIN

		    SELECT 1
		      INTO V_CNT
		      FROM IAS_QUT_PRM_MST
		     WHERE QUOT_SER = P_QT_SER AND P_DATE BETWEEN F_DATE AND T_DATE AND ROWNUM <= 1;
		EXCEPTION
		    WHEN OTHERS THEN
			V_CNT := 0;
		END;

		IF NVL(V_CNT, 0) = 0 THEN
		    V_INACTV := 1;
		ELSE
		    V_INACTV := 0;
		END IF;

	    END IF;

	    RETURN (V_INACTV);
	ELSE
	    RETURN (1);
	END IF;
    END CHK_QT_PRM_INACTV;

    PROCEDURE CLC_FREE_QTY_AS_DISC(P_DOC_SRL	     IN NUMBER,
				   P_DATE	     IN DATE,
				   P_BILL_DOC_TYPE   IN NUMBER,
				   P_CHK_QT_NO	     IN NUMBER DEFAULT NULL,
				   P_CHK_QT_SER      IN NUMBER DEFAULT NULL,
				   P_C_CODE	     IN VARCHAR2,
				   P_C_GROUP_CODE    IN NUMBER,
				   P_C_CLASS	     IN NUMBER,
				   P_C_DEGREE	     IN NUMBER,
				   P_C_CODE_CSH      IN VARCHAR2 DEFAULT NULL,
				   P_DOC_M_SQNC      IN VARCHAR2 DEFAULT NULL,
				   P_CST_GRP_CSH     IN NUMBER,
				   P_SYS_TYP	     IN NUMBER, --1- ONYX PRO 2- POS SYSTME 3- DTS SYSTEM 4- SHO SYSTEM
				   P_NO_OF_DCML      IN NUMBER,
				   P_CALL_TYP	     IN NUMBER DEFAULT 1)--# 1- CALC BEFORE SAVE 2-CALC AFTER SAVE
    IS
	L_QT_IQTY   NUMBER := 0;
    BEGIN
	IF CHK_QT_PRM_EXCPT(P_C_CODE => P_C_CODE) THEN
	    RETURN;
	END IF;

	FOR PRM_CRSR IN (SELECT DISTINCT
				M.QUOT_NO,
				M.QUOT_SER,
				M.QT_PRM_TYPE,
				M.QT_PRM_METHOD,
				M.QT_PRM_WC_TYPE,
				D.RCRD_NO,
				D.FREE_QTY,
				D.COMP_QTY,
				CASE WHEN M.QT_PRM_METHOD = 1 THEN I.G_CODE WHEN M.QT_PRM_METHOD = 2 THEN I.MNG_CODE WHEN M.QT_PRM_METHOD = 3 THEN I.SUBG_CODE WHEN M.QT_PRM_METHOD = 4 THEN I.ASSISTANT_NO WHEN M.QT_PRM_METHOD = 5 THEN I.DETAIL_NO WHEN M.QT_PRM_METHOD = 6 THEN TO_CHAR(I.GROUP_NO) WHEN M.QT_PRM_METHOD = 7 THEN TO_CHAR(I.ITEM_TYPE) WHEN M.QT_PRM_METHOD = 8 THEN I.GRP_CLASS_CODE WHEN M.QT_PRM_METHOD = 9 THEN TO_CHAR(D.PRM_GRP_NO) END ITM_CODE,
				CASE WHEN M.QT_PRM_WC_TYPE = 1 THEN D.W_CODE WHEN M.QT_PRM_WC_TYPE = 2 THEN D.WHG_CODE WHEN M.QT_PRM_WC_TYPE = 3 THEN D.CNTRY_NO WHEN M.QT_PRM_WC_TYPE = 4 THEN D.PROV_NO WHEN M.QT_PRM_WC_TYPE = 5 THEN D.CITY_NO WHEN M.QT_PRM_WC_TYPE = 6 THEN D.R_CODE END W_CODE,
				SUM(BD.I_QTY) I_QTY,
				TRUNC(SUM(BD.I_QTY) / D.COMP_QTY) * D.FREE_QTY QT_IQTY
			FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D, IAS_POS_HUNG_BILLS BD, IAS_ITM_MST I
			WHERE M.QUOT_SER = D.QUOT_SER
			    AND M.QT_PRM_TYPE IN (4, 5)
			    AND M.QUOT_SER = NVL(P_CHK_QT_SER, M.QUOT_SER)
			    AND BD.I_CODE = I.I_CODE
			    AND BD.BILL_SEQ = P_DOC_M_SQNC
			    AND (CASE
				    WHEN M.QT_PRM_METHOD = 1 AND D.G_CODE = I.G_CODE THEN 1
				    WHEN M.QT_PRM_METHOD = 2 AND D.MNG_CODE = I.MNG_CODE THEN 1
				    WHEN M.QT_PRM_METHOD = 3 AND D.SUBG_CODE = I.SUBG_CODE THEN 1
				    WHEN M.QT_PRM_METHOD = 4 AND D.ASSISTANT_NO = I.ASSISTANT_NO THEN 1
				    WHEN M.QT_PRM_METHOD = 5 AND D.DETAIL_NO = I.DETAIL_NO THEN 1
				    WHEN M.QT_PRM_METHOD = 6 AND D.GROUP_NO = I.GROUP_NO THEN 1
				    WHEN M.QT_PRM_METHOD = 7 AND D.ITEM_TYPE = I.ITEM_TYPE THEN 1
				    WHEN M.QT_PRM_METHOD = 8 AND D.GRP_CLASS_CODE = I.GRP_CLASS_CODE THEN 1
				    WHEN M.QT_PRM_METHOD = 9
				    AND EXISTS
					    (SELECT 1
						FROM IAS_QUT_PRM_GRP_DTL
						WHERE PRM_GRP_NO = D.PRM_GRP_NO AND I_CODE = BD.I_CODE AND ROWNUM <= 1) THEN 1
				    ELSE 0
				END) = 1
			    AND (CASE
				    WHEN M.QT_PRM_WC_TYPE = 0 THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 1 AND D.W_CODE = BD.W_CODE THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 2
				    AND D.WHG_CODE = (SELECT WHG_CODE
							FROM WAREHOUSE_DETAILS
							WHERE W_CODE = BD.W_CODE AND ROWNUM <= 1) THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 3
				    AND D.CNTRY_NO = (SELECT CNTRY_NO
							FROM WAREHOUSE_DETAILS
							WHERE W_CODE = BD.W_CODE AND ROWNUM <= 1) THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 4
				    AND D.PROV_NO = (SELECT PROV_NO
							FROM WAREHOUSE_DETAILS
							WHERE W_CODE = BD.W_CODE AND ROWNUM <= 1) THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 5
				    AND D.CITY_NO = (SELECT CITY_NO
							FROM WAREHOUSE_DETAILS
							WHERE W_CODE = BD.W_CODE AND ROWNUM <= 1) THEN 1
				    WHEN M.QT_PRM_WC_TYPE = 6
				    AND D.R_CODE = (SELECT R_CODE
							FROM WAREHOUSE_DETAILS
						    WHERE W_CODE = BD.W_CODE AND ROWNUM <= 1) THEN 1
				    ELSE 0
				END) = 1
			    AND (CASE
				     WHEN M.QT_PRM_CST_TYPE = 0 THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 1 AND D.C_CODE = P_C_CODE THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 2 AND D.C_GROUP_CODE = P_C_GROUP_CODE THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 3 AND D.C_CLASS = P_C_CLASS THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 4 AND D.C_DEGREE = P_C_DEGREE THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 5 AND P_C_CODE_CSH IS NOT NULL THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 7 AND D.CUST_GRP_CODE = P_CST_GRP_CSH THEN 1
				     WHEN M.QT_PRM_CST_TYPE = 8 AND D.C_CODE_CSH = P_C_CODE_CSH THEN 1
				     ELSE 0
				 END) = 1
			    AND (CASE
				    WHEN QT_PRM_CST_TYPE IN (2, 3, 4)
				    AND P_C_CODE IS NOT NULL
				    AND P_C_CODE IN (SELECT C_CODE
							FROM IAS_CST_EXPT_QUT_PRM
							WHERE QUOT_SER = M.QUOT_SER) THEN 0
				    ELSE 1
				END) = 1
			    AND NVL(M.INACTIVE, 0) = 0
			    AND NVL(M.APPROVED, 0) = 1
			    AND CASE WHEN P_BILL_DOC_TYPE IN (4,8) THEN 2 ELSE 1 END = CASE WHEN M.BILL_DOC_TYPE IS NULL THEN CASE WHEN P_BILL_DOC_TYPE IN (4,8) THEN 2 ELSE 1 END ELSE M.BILL_DOC_TYPE END
			    AND P_DATE BETWEEN F_DATE AND T_DATE
			    AND TO_CHAR(TO_DATE(P_DATE), 'D') IN (FLD_DAY1, FLD_DAY2, FLD_DAY3, FLD_DAY4, FLD_DAY5, FLD_DAY6, FLD_DAY7)
			    AND CASE NVL(P_SYS_TYP, 1)
				    WHEN 1 THEN NVL(USE_QTN_PRM_IN_IAS_SYS_FLG, 0)
				    WHEN 2 THEN NVL(USE_QTN_PRM_IN_POS_SYS_FLG, 0)
				    WHEN 3 THEN NVL(USE_QTN_PRM_IN_DTS_SYS_FLG, 0)
				    WHEN 4 THEN NVL(USE_QTN_PRM_IN_SHO_SYS_FLG, 0)
				    WHEN 5 THEN NVL(USE_QTN_PRM_IN_RMS_SYS_FLG, 0)
				    ELSE NVL(USE_QTN_PRM_IN_IAS_SYS_FLG, 0)
				END = 1
			    AND TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS') >= NVL(M.F_TIME, TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS'))
			    AND TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS') <= NVL(M.T_TIME, TO_CHAR(IAS_GEN_PKG.GET_CURDATE, 'HH24:MI:SS'))
		    GROUP BY M.QUOT_NO,
			     M.QUOT_SER,
			     M.QT_PRM_TYPE,
			     M.QT_PRM_METHOD,
			     M.QT_PRM_WC_TYPE,
			     D.RCRD_NO,
			     D.FREE_QTY,
			     D.COMP_QTY,
			     CASE WHEN M.QT_PRM_METHOD = 1 THEN I.G_CODE WHEN M.QT_PRM_METHOD = 2 THEN I.MNG_CODE WHEN M.QT_PRM_METHOD = 3 THEN I.SUBG_CODE WHEN M.QT_PRM_METHOD = 4 THEN I.ASSISTANT_NO WHEN M.QT_PRM_METHOD = 5 THEN I.DETAIL_NO WHEN M.QT_PRM_METHOD = 6 THEN TO_CHAR(I.GROUP_NO) WHEN M.QT_PRM_METHOD = 7 THEN TO_CHAR(I.ITEM_TYPE) WHEN M.QT_PRM_METHOD = 8 THEN I.GRP_CLASS_CODE WHEN M.QT_PRM_METHOD = 9 THEN TO_CHAR(D.PRM_GRP_NO) END,
			     CASE WHEN M.QT_PRM_WC_TYPE = 1 THEN D.W_CODE WHEN M.QT_PRM_WC_TYPE = 2 THEN D.WHG_CODE WHEN M.QT_PRM_WC_TYPE = 3 THEN D.CNTRY_NO WHEN M.QT_PRM_WC_TYPE = 4 THEN D.PROV_NO WHEN M.QT_PRM_WC_TYPE = 5 THEN D.CITY_NO WHEN M.QT_PRM_WC_TYPE = 6 THEN D.R_CODE END
		     HAVING SUM(BD.I_QTY) >= NVL(D.COMP_QTY, 0)
		     ORDER BY (TRUNC(SUM(BD.I_QTY) / D.COMP_QTY) * D.FREE_QTY) DESC)
	LOOP

	    BEGIN
		L_QT_IQTY := PRM_CRSR.QT_IQTY;

		FOR ITM_CRSR IN (SELECT D.I_CODE,
					D.I_QTY,
					D.I_PRICE,
					D.RCRD_NO,
					D.DOC_D_SEQ
				   FROM IAS_POS_HUNG_BILLS D, IAS_ITM_MST M, IAS_QUT_PRM_DTL Q
				  WHERE D.BILL_SEQ = P_DOC_M_SQNC
				    AND D.I_CODE = M.I_CODE
				    AND Q.QUOT_SER = PRM_CRSR.QUOT_SER
				    AND Q.RCRD_NO = PRM_CRSR.RCRD_NO
				    AND Q.QUOT_SER = NVL(P_CHK_QT_SER, Q.QUOT_SER)
				    AND CASE NVL(PRM_CRSR.QT_PRM_METHOD, 0) WHEN 9 THEN NVL((SELECT 1
											FROM IAS_QUT_PRM_GRP_DTL
										       WHERE PRM_GRP_NO = Q.PRM_GRP_NO AND I_CODE = D.I_CODE AND ROWNUM <= 1),0) ELSE 1 END = 1
				    AND CASE WHEN PRM_CRSR.QT_PRM_METHOD = 1 THEN M.G_CODE WHEN PRM_CRSR.QT_PRM_METHOD = 2 THEN M.MNG_CODE WHEN PRM_CRSR.QT_PRM_METHOD = 3 THEN M.SUBG_CODE WHEN PRM_CRSR.QT_PRM_METHOD = 4 THEN M.ASSISTANT_NO WHEN PRM_CRSR.QT_PRM_METHOD = 5 THEN M.DETAIL_NO WHEN PRM_CRSR.QT_PRM_METHOD = 6 THEN TO_CHAR(M.GROUP_NO) WHEN PRM_CRSR.QT_PRM_METHOD = 7 THEN TO_CHAR(M.ITEM_TYPE) WHEN PRM_CRSR.QT_PRM_METHOD = 8 THEN M.GRP_CLASS_CODE WHEN PRM_CRSR.QT_PRM_METHOD = 9 THEN TO_CHAR(Q.PRM_GRP_NO) END = PRM_CRSR.ITM_CODE
				    AND CASE WHEN PRM_CRSR.QT_PRM_WC_TYPE = 1 THEN Q.W_CODE WHEN PRM_CRSR.QT_PRM_WC_TYPE = 2 THEN Q.WHG_CODE WHEN PRM_CRSR.QT_PRM_WC_TYPE = 3 THEN Q.CNTRY_NO WHEN PRM_CRSR.QT_PRM_WC_TYPE = 4 THEN Q.PROV_NO WHEN PRM_CRSR.QT_PRM_WC_TYPE = 5 THEN Q.CITY_NO WHEN PRM_CRSR.QT_PRM_WC_TYPE = 6 THEN Q.R_CODE ELSE 0 END = NVL(PRM_CRSR.W_CODE, 0)
			       ORDER BY CASE WHEN PRM_CRSR.QT_PRM_TYPE = 4 THEN I_PRICE ELSE I_PRICE * -1 END ASC,D.RCRD_NO)
		LOOP
		    IF ITM_CRSR.I_QTY > L_QT_IQTY THEN
			IF P_CALL_TYP = 2 THEN
			    UPDATE IAS_POS_BILL_DTL
			       SET DIS_AMT_DTL = (NVL(ITM_CRSR.I_PRICE, 0) / ITM_CRSR.I_QTY) * NVL(L_QT_IQTY, 0), DIS_PER = (NVL((NVL(ITM_CRSR.I_PRICE, 0) / ITM_CRSR.I_QTY) * NVL(L_QT_IQTY, 0), 0) / NVL(I_PRICE, 0)) * 100, QT_PRM_NO = PRM_CRSR.QUOT_NO, QT_PRM_SER = PRM_CRSR.QUOT_SER, QT_PRM_RCRD_NO = PRM_CRSR.RCRD_NO
			     WHERE BILL_SRL = P_DOC_SRL AND I_CODE = ITM_CRSR.I_CODE AND RCRD_NO = ITM_CRSR.RCRD_NO AND DOC_D_SEQ = ITM_CRSR.DOC_D_SEQ;
			ELSE
			    UPDATE IAS_POS_HUNG_BILLS
			       SET DIS_AMT_DTL = (NVL(ITM_CRSR.I_PRICE, 0) / ITM_CRSR.I_QTY) * NVL(L_QT_IQTY, 0), DIS_PER = (NVL((NVL(ITM_CRSR.I_PRICE, 0) / ITM_CRSR.I_QTY) * NVL(L_QT_IQTY, 0), 0) / NVL(I_PRICE, 0)) * 100, QT_PRM_NO = PRM_CRSR.QUOT_NO, QT_PRM_SER = PRM_CRSR.QUOT_SER, QT_PRM_RCRD_NO = PRM_CRSR.RCRD_NO,QT_PRM_METHOD = PRM_CRSR.QT_PRM_METHOD,CHNG_FLG = 1
			     WHERE BILL_SEQ = P_DOC_M_SQNC AND I_CODE = ITM_CRSR.I_CODE AND RCRD_NO = ITM_CRSR.RCRD_NO AND DOC_D_SEQ = ITM_CRSR.DOC_D_SEQ;
			END IF;

			L_QT_IQTY := 0;
		    ELSE
			IF P_CALL_TYP = 2 THEN
			    UPDATE IAS_POS_BILL_DTL
			       SET DIS_AMT_DTL = NVL(ITM_CRSR.I_PRICE, 0), DIS_PER = 100, QT_PRM_NO = PRM_CRSR.QUOT_NO, QT_PRM_SER = PRM_CRSR.QUOT_SER, QT_PRM_RCRD_NO = PRM_CRSR.RCRD_NO
			     WHERE BILL_SRL = P_DOC_SRL AND I_CODE = ITM_CRSR.I_CODE AND RCRD_NO = ITM_CRSR.RCRD_NO AND DOC_D_SEQ = ITM_CRSR.DOC_D_SEQ;
			ELSE
			    UPDATE IAS_POS_HUNG_BILLS
			       SET DIS_AMT_DTL = NVL(ITM_CRSR.I_PRICE, 0), DIS_PER = 100, QT_PRM_NO = PRM_CRSR.QUOT_NO, QT_PRM_SER = PRM_CRSR.QUOT_SER, QT_PRM_RCRD_NO = PRM_CRSR.RCRD_NO,QT_PRM_METHOD = PRM_CRSR.QT_PRM_METHOD,CHNG_FLG = 1
			     WHERE BILL_SEQ = P_DOC_M_SQNC AND I_CODE = ITM_CRSR.I_CODE AND RCRD_NO = ITM_CRSR.RCRD_NO AND DOC_D_SEQ = ITM_CRSR.DOC_D_SEQ;
			END IF;

			L_QT_IQTY := L_QT_IQTY - ITM_CRSR.I_QTY;
		    END IF;

		    IF L_QT_IQTY = 0 THEN
			EXIT;
		    END IF;

		END LOOP;
		EXIT;
	    END;

	END LOOP;

	BEGIN
	    IF P_CALL_TYP = 2 THEN
		UPDATE IAS_POS_BILL_MST
		   SET DISC_AMT_DTL = (SELECT ROUND(NVL(SUM(I_QTY * DIS_AMT_DTL), 0), P_NO_OF_DCML)
					  FROM IAS_POS_BILL_DTL
					 WHERE BILL_SRL = P_DOC_SRL),
		       DISC_AMT = ROUND(  NVL((SELECT ROUND(NVL(SUM(I_QTY * DIS_AMT_DTL), 0), P_NO_OF_DCML)
						 FROM IAS_POS_BILL_DTL
						WHERE BILL_SRL = P_DOC_SRL),
					      0) + NVL(DISC_AMT_MST, 0), P_NO_OF_DCML)
		 WHERE BILL_SRL = P_DOC_SRL;
	    END IF;
	EXCEPTION
	    WHEN OTHERS THEN
		NULL;
	END;

    EXCEPTION
	WHEN OTHERS THEN
	    NULL;
    END CLC_FREE_QTY_AS_DISC;

