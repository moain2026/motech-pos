    PROCEDURE GET_DFLT_ITM_PRICE(P_I_CODE	  IN	 IAS_POS_BILL_DTL.I_CODE%TYPE,
				 P_ITM_UNT	  IN	 IAS_POS_BILL_DTL.ITM_UNT%TYPE,
				 P_I_QTY	  IN	 IAS_POS_BILL_DTL.I_QTY%TYPE,
				 P_PRICE_LVL	  IN	 NUMBER,
				 P_I_PRICE	     OUT IAS_POS_BILL_DTL.I_PRICE%TYPE,
				 P_I_PRICE_VAT	     OUT IAS_POS_BILL_DTL.I_PRICE%TYPE,
				 P_VAT_AMT	     OUT IAS_POS_BILL_DTL.VAT_AMT%TYPE,
				 P_VAT_PER	     OUT IAS_POS_BILL_DTL.VAT_PER%TYPE)
    IS
    BEGIN
	BEGIN
	     P_I_PRICE := ROUND(NVL(IAS_GEN_PKG.GET_ITM_PRICE_DOCACY(P_ICODE	     => P_I_CODE,
								     P_ITM_UNT	     => P_ITM_UNT,
								     P_ACY	     => G_A_CY,
								     P_LOC_CUR	     => G_LOCAL_CUR,
								     P_STK_CUR	     => G_STK_CUR,
								     P_AC_RATE	     => G_PRICE_RATE,
								     P_STK_RATE      => G_STK_RATE,
								     P_LEV_NO	     => P_PRICE_LVL,
								     P_WCODE	     => G_DEF_WCODE,
								     --P_BILL_RATE     => NVL(G_BILL_RATE,1),
								     P_IQTY	     => NVL(P_I_QTY, 1),
								     P_BILL_DOC_TYPE => G_BILL_DOC_TYPE,
								     P_PRICE_TYPE    => G_PRICE_TYPE,
								     P_BRN_NO	     => G_BRN_NO),0),G_NO_OF_DECIMAL);

	EXCEPTION
	    WHEN OTHERS THEN
		RAISE_APPLICATION_ERROR(-20502,'EXCEPTION IN GET_ITM_PRICE_DOCACY '||SQLERRM);
	END;

	IF NVL (YS_TAX_PKG.GET_CLC_TAX_TYP ( G_CLC_TYP_NO_TAX), 0) <> 0 THEN
	    P_VAT_PER := 0;
	    P_VAT_AMT := NULL;
	END IF;

	IF P_I_PRICE > 0 THEN

	    IF G_USE_VAT = 1 THEN
		P_VAT_PER := NVL(YS_TAX_PKG.GET_ITM_TAX_PRCNT(P_CLC_TYP_NO => G_CLC_TYP_NO_TAX, P_I_CODE => P_I_CODE, P_CLC_USD_ITM => 0),0);
	    END IF;

	    IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
		P_I_PRICE_VAT := P_I_PRICE;
		P_I_PRICE := ROUND( P_I_PRICE_VAT / ((NVL( P_VAT_PER, 0) / 100) + 1), 15);
	    ELSE
		P_I_PRICE_VAT := 0;
	    END IF;

	END IF;

	IF G_USE_VAT = 1 THEN
	    P_VAT_AMT := ROUND(P_I_PRICE * (P_VAT_PER/100),15);
	END IF;

    END GET_DFLT_ITM_PRICE;

