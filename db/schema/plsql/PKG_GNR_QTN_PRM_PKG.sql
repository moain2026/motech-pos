		     WHERE IAS_QUT_PRM_MST.QUOT_SER = IAS_QUT_PRM_DTL.QUOT_SER
		       AND IAS_QUT_PRM_MST.QUOT_SER = P_QT_SER
		       AND IAS_QUT_PRM_DTL.I_CODE = P_ICODE
		       AND IAS_QUT_PRM_DTL.ITM_UNT = P_ITMUNT
		       AND IAS_QUT_PRM_DTL.F_QTY <= P_IQTY
		       AND NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0') = DECODE(NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0'), '0', NVL(IAS_QUT_PRM_DTL.BATCH_NO, '0'), NVL(P_BATCH_NO, '0'))
		       AND NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900') =
			   DECODE(NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900'), '01/01/1900', NVL(IAS_QUT_PRM_DTL.EXPIRE_DATE, '01/01/1900'), NVL(P_EXP_DATE, '01/01/1900'))
		       AND (CASE
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 0 THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 1 AND IAS_QUT_PRM_DTL.C_CODE = P_C_CODE THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 2 AND IAS_QUT_PRM_DTL.C_GROUP_CODE = P_C_GROUP_CODE THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 3 AND IAS_QUT_PRM_DTL.C_CLASS = P_C_CLASS THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 4 AND IAS_QUT_PRM_DTL.C_DEGREE = P_C_DEGREE THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 5 AND P_C_CODE_CSH IS NOT NULL THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 7 AND IAS_QUT_PRM_DTL.CUST_GRP_CODE = P_CST_GRP_CSH THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_CST_TYPE = 8 AND IAS_QUT_PRM_DTL.C_CODE_CSH = P_C_CODE_CSH THEN 1
				ELSE 0
			    END) = 1
		       AND (CASE
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 0 THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 1 AND IAS_QUT_PRM_DTL.W_CODE = P_WCODE THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 2
				 AND IAS_QUT_PRM_DTL.WHG_CODE = (SELECT WHG_CODE
								   FROM WAREHOUSE_DETAILS
								  WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 3
				 AND IAS_QUT_PRM_DTL.CNTRY_NO = (SELECT CNTRY_NO
								   FROM WAREHOUSE_DETAILS
								  WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 4
				 AND IAS_QUT_PRM_DTL.PROV_NO = (SELECT PROV_NO
								  FROM WAREHOUSE_DETAILS
								 WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 5
				 AND IAS_QUT_PRM_DTL.CITY_NO = (SELECT CITY_NO
								  FROM WAREHOUSE_DETAILS
								 WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
				WHEN IAS_QUT_PRM_MST.QT_PRM_WC_TYPE = 6
				 AND IAS_QUT_PRM_DTL.R_CODE = (SELECT R_CODE
								 FROM WAREHOUSE_DETAILS
								WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
				ELSE 0
			    END) = 1
		       AND (CASE WHEN NVL(IAS_QUT_PRM_MST.BY_LEV_NO,0) = 0 THEN 1
				 WHEN NVL(IAS_QUT_PRM_MST.BY_LEV_NO,0) = 1 AND IAS_QUT_PRM_DTL.LEV_NO = NVL(P_LEV_NO,0) THEN 1 ELSE 0 END) = 1
		       AND 1 = (CASE WHEN NVL(IAS_QUT_PRM_MST.BY_EXP_DAY, 0) = 0 OR (NVL(IAS_QUT_PRM_MST.BY_EXP_DAY, 0) = 1 AND IAS_QUT_PRM_DTL.EXP_DAY IS NULL) THEN 1 WHEN NVL(IAS_QUT_PRM_MST.BY_EXP_DAY, 0) = 1 AND IAS_QUT_PRM_DTL.EXP_DAY IS NOT NULL AND NVL(IAS_QUT_PRM_DTL.EXP_DAY, 0) >= (NVL(P_EXP_DATE, '01/01/1900') - TO_DATE(SYSDATE, 'DD/MM/YYYY')) THEN 1 ELSE 0 END);

		ELSIF P_QT_SER IS NOT NULL AND P_QT_PRM_TYPE = 2 AND P_QT_PRM_METHOD = 1 AND V_CALC_ALL_SLIDES = 1 THEN --- (4)

		    ----------------------------------------------------------
		    --## Get Promotion If Use Monthly Accumulative
		    ----------------------------------------------------------
		    BEGIN
			V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

			SELECT SUM(NVL(I_QTY, 0))
			  INTO V_IQTY_CMLTV
			  FROM IAS_POS_HUNG_BILLS
			 WHERE I_CODE = P_ICODE AND DOC_D_SEQ <= P_DOC_SEQ_TMP;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_IQTY_CMLTV := 0;
		    END;

		    V_AVG_PRICE := 0;

		    FOR I IN NVL(V_IQTY_CMLTV, 0) - NVL(P_IQTY, 0) + 1 .. NVL(V_IQTY_CMLTV, 0)
		    LOOP

			BEGIN
			    V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

			    SELECT NVL(IAS_QUT_PRM_DTL.I_PRICE, 0)
			      INTO P_PRICE
			      FROM IAS_QUT_PRM_MST, IAS_QUT_PRM_DTL
			     WHERE IAS_QUT_PRM_MST.QUOT_SER = IAS_QUT_PRM_DTL.QUOT_SER
			       AND IAS_QUT_PRM_MST.QUOT_SER = P_QT_SER
			       AND IAS_QUT_PRM_DTL.I_CODE = P_ICODE
			       AND IAS_QUT_PRM_DTL.ITM_UNT = P_ITMUNT
			       AND I BETWEEN IAS_QUT_PRM_DTL.F_QTY AND IAS_QUT_PRM_DTL.T_QTY
			       AND (CASE WHEN NVL(IAS_QUT_PRM_MST.BY_INVC_AMT_FLG,0) = 1 AND IAS_QUT_PRM_DTL.BILL_AMT IS NOT NULL AND NVL(P_BILL_AMT,0) >= IAS_QUT_PRM_DTL.BILL_AMT THEN 1 WHEN NVL(IAS_QUT_PRM_MST.BY_INVC_AMT_FLG,0) = 0 THEN 1 ELSE 0 END) = 1
			       AND (CASE WHEN NVL(IAS_QUT_PRM_MST.BY_LEV_NO,0) = 0 THEN 1
					 WHEN NVL(IAS_QUT_PRM_MST.BY_LEV_NO,0) = 1 AND IAS_QUT_PRM_DTL.LEV_NO = NVL(P_LEV_NO,0) THEN 1 ELSE 0 END) = 1;
			EXCEPTION
			    WHEN OTHERS THEN
				P_PRICE := 0;
			END;

			--If Nvl(P_Price,0)>0 Then
			V_AVG_PRICE := ((NVL(V_AVG_PRICE, 0) * NVL(V_QTY, 0)) + NVL(P_PRICE, 0)) / (NVL(V_QTY, 0) + 1);
			--End If;
			V_QTY := NVL(V_QTY, 0) + 1;

		    END LOOP;

		    BEGIN
			V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

			SELECT MAX(NVL(IAS_QUT_PRM_DTL.T_QTY, 0))
			  INTO V_MAX_QTY
			  FROM IAS_QUT_PRM_MST, IAS_QUT_PRM_DTL
			 WHERE IAS_QUT_PRM_MST.QUOT_SER = IAS_QUT_PRM_DTL.QUOT_SER AND IAS_QUT_PRM_MST.QUOT_SER = P_QT_SER AND IAS_QUT_PRM_DTL.I_CODE = P_ICODE AND IAS_QUT_PRM_DTL.ITM_UNT = P_ITMUNT;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_MAX_QTY := 0;
		    END;

		    V_MQTY := 0;

		    IF NVL(V_IQTY_CMLTV, 0) - NVL(V_MAX_QTY, 0) > NVL(P_IQTY, 0) THEN
			V_MQTY := NVL(P_IQTY, 0);
		    ELSE
			V_MQTY := NVL(V_IQTY_CMLTV, 0) - NVL(V_MAX_QTY, 0);
		    END IF;

		    V_QTY := NVL(P_IQTY, 0) - NVL(V_MQTY, 0);

		    IF NVL(V_IQTY_CMLTV, 0) > NVL(V_MAX_QTY, 0) THEN
			P_PRICE := ((NVL(V_AVG_PRICE, 0) * NVL(V_QTY, 0)) + (NVL(P_IPRICE, 0) * NVL(V_MQTY, 0))) / NVL(P_IQTY, 0);
		    ELSE
			P_PRICE := NVL(V_AVG_PRICE, 0);
		    END IF;
		ELSIF P_QT_SER IS NOT NULL AND P_QT_PRM_TYPE = 2 AND P_QT_PRM_METHOD = 4 AND NVL(V_BY_COMP_QTY, 0) = 1 AND NVL(V_CMLTV_MNTH_FLG, 0) = 1 THEN

		    BEGIN
			V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

			SELECT SUM(NVL(D.P_QTY, 0) / NVL(V_P_SIZE_QT, 1))
			  INTO V_QTY
			  FROM IAS_POS_BILL_MST M, IAS_POS_BILL_DTL D, WAREHOUSE_DETAILS W
			 WHERE M.BILL_SRL = D.BILL_SRL
			   AND M.BILL_SRL <> NVL(P_BILL_SER, 0)
			   AND M.C_CODE = P_C_CODE
			   AND TO_CHAR(M.BILL_DATE, 'MM/YYYY') = TO_CHAR(P_DATE, 'MM/YYYY')
			   AND D.I_CODE = P_ICODE
			   AND D.W_CODE = W.W_CODE
			   AND (CASE
				    WHEN V_QT_PRM_WC_TYPE = 0 THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 1 AND D.W_CODE = P_WCODE THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 2 AND V_WHG_CODE = W.WHG_CODE THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 3 AND V_CNTRY_NO = W.CNTRY_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 4 AND V_PROV_NO = W.PROV_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 5 AND V_CITY_NO = W.CITY_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 6 AND V_R_CODE = W.R_CODE THEN 1
				    ELSE 0
				END) = 1
			   AND (CASE WHEN V_BY_LEV_NO = 0 THEN 1
				     WHEN V_BY_LEV_NO = 1 AND NVL(D.PRICE_LVL,0) = NVL(P_LEV_NO,0) THEN 1 ELSE 0 END) = 1;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_QTY := 0;
		    END;

		    BEGIN
			V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

			SELECT SUM(NVL(D.I_QTY, 0) * NVL(D.P_SIZE, 0) / NVL(V_P_SIZE_QT, 1))
			  INTO V_IQTY
			  FROM IAS_POS_HUNG_BILLS D, WAREHOUSE_DETAILS W
			 WHERE D.W_CODE = W.W_CODE
			   AND D.I_CODE = P_ICODE
			   AND (CASE
				    WHEN V_QT_PRM_WC_TYPE = 0 THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 1 AND D.W_CODE = P_WCODE THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 2 AND V_WHG_CODE = W.WHG_CODE THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 3 AND V_CNTRY_NO = W.CNTRY_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 4 AND V_PROV_NO = W.PROV_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 5 AND V_CITY_NO = W.CITY_NO THEN 1
				    WHEN V_QT_PRM_WC_TYPE = 6 AND V_R_CODE = W.R_CODE THEN 1
				    ELSE 0
				END) = 1
			   AND (CASE WHEN V_BY_LEV_NO = 0 THEN 1
				     WHEN V_BY_LEV_NO = 1 AND NVL(D.PRICE_LVL,0) = NVL(P_LEV_NO,0) THEN 1 ELSE 0 END) = 1;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_IQTY := 0;
		    END;

		    V_QTY := NVL(MOD(V_QTY, V_COMP_QTY), 0) + NVL(V_IQTY, 0);

		    P_FQTY := TRUNC(TRUNC(V_QTY / V_COMP_QTY) * P_FQTY);
		ELSIF P_QT_SER IS NOT NULL AND P_QT_PRM_TYPE = 1 AND P_QT_PRM_METHOD = 1 AND NVL(V_BY_COMP_QTY, 0) = 1 AND P_IQTY IS NOT NULL AND P_PRICE IS NOT NULL AND P_IPRICE IS NOT NULL THEN
		    P_PRICE := ((MOD(P_IQTY, V_COMP_QTY) * P_IPRICE) + ((P_IQTY - MOD(P_IQTY, V_COMP_QTY)) * P_PRICE)) / P_IQTY;
		END IF; --- (4)

	    END IF; --- (3)

	    ----------------------------------------------------------
	    --## Get Remain Quantity To Promotion
	    ----------------------------------------------------------
	    IF (V_QT_QTY IS NOT NULL AND (P_IQTY > (NVL(V_QT_QTY, 0) - NVL(V_QT_QTY_BILL, 0)))) OR (P_QT_PRM_METHOD = 4 AND V_BY_COMP_QTY = 1 AND P_FQTY = 0) THEN

		P_QT_REM_QTY := (NVL(V_QT_QTY, 0) - NVL(V_QT_QTY_BILL, 0));

		IF P_QT_PRM_TYPE = 1 AND P_QT_PRM_METHOD = 2 AND NVL(P_QT_REM_QTY, 0) > 0 THEN
		    IF P_DIS_PER IS NULL THEN
			P_DIS_AMT := (P_QT_REM_QTY / P_IQTY) * P_DIS_AMT;
		    ELSE
			P_DIS_PER := (P_QT_REM_QTY / P_IQTY) * P_DIS_PER;
		    END IF;
		ELSIF P_QT_PRM_TYPE = 1 AND P_QT_PRM_METHOD = 1 AND NVL(P_QT_REM_QTY, 0) > 0 THEN
		    P_PRICE := ((P_QT_REM_QTY * P_PRICE) + ((P_IQTY - P_QT_REM_QTY) * P_IPRICE)) / P_IQTY;
		ELSE
		    P_QT_NO := NULL;
		    P_QT_SER := NULL;
		    P_QT_ICODE := NULL;
		    P_QT_ITM_UNT := NULL;
		    P_QT_RCRD_NO := NULL;
		    P_DIS_PER := NULL;
		    P_DIS_AMT := NULL;
		    P_PRICE := P_IPRICE;
		    P_FQTY := NULL;
		END IF;
	    END IF;
	END IF; --- (1)

	---------------------------------------------------------------------
	--## Get Discount For Items If Use Calculate Discount On Bills Levels
	---------------------------------------------------------------------
	IF P_QT_SER IS NOT NULL AND P_QT_PRM_TYPE = 3 AND P_QT_PRM_METHOD = 1 AND P_QT_PRM_ITM_TYPE IS NOT NULL THEN

	    BEGIN
		V_LAST_EXECUTED_LINE := $$PLSQL_LINE;

		SELECT DECODE(D.DISC_TYPE, 1, D.DISC_AMT_PER), DECODE(D.DISC_TYPE, 2, NVL(ROUND(D.DISC_AMT_PER * (SELECT IAS_GEN_PKG.GET_CUR_RATE(M.QUOT_CUR) FROM DUAL) / P_BILL_RATE, P_NO_OF_DCML), 0)) --1306
		  INTO P_DIS_PER, P_DIS_AMT
		  FROM IAS_QUT_PRM_MST M, IAS_QUT_PRM_DTL D, IAS_ITM_MST MI, IAS_ITM_DTL DI
		 WHERE M.QUOT_SER = D.QUOT_SER
		   AND MI.I_CODE = DI.I_CODE
		   AND M.QUOT_SER = P_QT_SER
		   AND MI.I_CODE = P_ICODE
		   AND DI.ITM_UNT = P_ITMUNT
		   AND P_BILL_AMT BETWEEN NVL(ROUND(D.F_AMT * (SELECT IAS_GEN_PKG.GET_CUR_RATE(M.QUOT_CUR) FROM DUAL) / P_BILL_RATE, P_NO_OF_DCML), 0)
				      AND NVL(ROUND(D.T_AMT * (SELECT IAS_GEN_PKG.GET_CUR_RATE(M.QUOT_CUR) FROM DUAL) / P_BILL_RATE, P_NO_OF_DCML), 0)
		   AND (CASE
			    WHEN P_QT_PRM_ITM_TYPE = 1 AND D.G_CODE = MI.G_CODE THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 2 AND D.MNG_CODE = MI.MNG_CODE THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 3 AND D.SUBG_CODE = MI.SUBG_CODE THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 4 AND D.ASSISTANT_NO = MI.ASSISTANT_NO THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 5 AND D.DETAIL_NO = MI.DETAIL_NO THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 6 AND D.GROUP_NO = MI.GROUP_NO THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 7 AND D.ITEM_TYPE = MI.ITEM_TYPE THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 8 AND D.GRP_CLASS_CODE = MI.GRP_CLASS_CODE THEN 1
			    WHEN P_QT_PRM_ITM_TYPE = 9
			     AND EXISTS
				     (SELECT 1
					FROM IAS_QUT_PRM_GRP_DTL
				       WHERE PRM_GRP_NO = D.PRM_GRP_NO AND I_CODE = P_ICODE AND ROWNUM <= 1) THEN 1
			    ELSE 0
			END) = 1
		   AND NVL(D.BATCH_NO, '0') = DECODE(NVL(D.BATCH_NO, '0'), '0', NVL(D.BATCH_NO, '0'), NVL(P_BATCH_NO, '0'))
		   AND NVL(D.EXPIRE_DATE, '01/01/1900') = DECODE(NVL(D.EXPIRE_DATE, '01/01/1900'), '01/01/1900', NVL(D.EXPIRE_DATE, '01/01/1900'), NVL(P_EXP_DATE, '01/01/1900'))
		   AND (CASE
			    WHEN M.QT_PRM_WC_TYPE = 0 THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 1 AND D.W_CODE = P_WCODE THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 2
			     AND D.WHG_CODE = (SELECT WHG_CODE
						 FROM WAREHOUSE_DETAILS
						WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 3
			     AND D.CNTRY_NO = (SELECT CNTRY_NO
						 FROM WAREHOUSE_DETAILS
						WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 4
			     AND D.PROV_NO = (SELECT PROV_NO
						FROM WAREHOUSE_DETAILS
					       WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 5
			     AND D.CITY_NO = (SELECT CITY_NO
						FROM WAREHOUSE_DETAILS
					       WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
			    WHEN M.QT_PRM_WC_TYPE = 6
			     AND D.R_CODE = (SELECT R_CODE
					       FROM WAREHOUSE_DETAILS
					      WHERE W_CODE = P_WCODE AND ROWNUM <= 1) THEN 1
			    ELSE 0
			END) = 1
		   AND (CASE
			    WHEN M.QT_PRM_CST_TYPE = 0 THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 1 AND D.C_CODE = P_C_CODE THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 2 AND D.C_GROUP_CODE = P_C_GROUP_CODE THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 3 AND D.C_CLASS = P_C_CLASS THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 4 AND D.C_DEGREE = P_C_DEGREE THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 5 AND P_C_CODE_CSH IS NOT NULL THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 7 AND D.CUST_GRP_CODE = P_CST_GRP_CSH THEN 1
			    WHEN M.QT_PRM_CST_TYPE = 8 AND D.C_CODE_CSH = P_C_CODE_CSH THEN 1
			    ELSE 0
			END) = 1
		   AND (CASE WHEN NVL(M.BY_LEV_NO,0) = 0 THEN 1
			     WHEN NVL(M.BY_LEV_NO,0) = 1 AND D.LEV_NO = NVL(P_LEV_NO,0) THEN 1 ELSE 0 END) = 1	
		   AND ROWNUM <= 1;
	    EXCEPTION
		WHEN OTHERS THEN
		    P_QT_NO := NULL;
		    P_QT_SER := NULL;
		    P_QT_PRM_TYPE := NULL;
		    P_QT_PRM_METHOD := NULL;
		    P_QT_PRM_ITM_TYPE := NULL;
		    P_QT_RCRD_NO := NULL;
		    P_DIS_PER := NULL;
		    P_DIS_AMT := NULL;
	    END;

	END IF;
    EXCEPTION
	WHEN OTHERS THEN
	    DEBUG_PRC('EXCEPTION IN  IAS_GET_QT_PRM >>'||SQLERRM ||' - AT LINE -'||V_LAST_EXECUTED_LINE);
    END IAS_GET_QT_PRM;

    FUNCTION CHK_USNG_TAX(P_BRN_NO IAS_POS_MACHINE.DEF_BRN_NO%TYPE,P_MACHINE_NO IAS_POS_MACHINE.MACHINE_NO%TYPE)
	RETURN NUMBER--## 0-NOT USING TAX 1-USE TAX 2-USE TAX PRICE INCLUDE VAT
    IS
	L_TAX_FLG NUMBER(3) := 0;
    BEGIN
	SELECT (CASE
		    WHEN (CASE NVL(USE_VAT, 0)
			      WHEN 1 THEN
				  (NVL((SELECT NVL(USE_VAT, 0)
					  FROM IAS_POS_MACHINE
					 WHERE MACHINE_NO = P_MACHINE_NO),
				       0))
			      ELSE 0
			  END) > 0 THEN
			NVL(USE_PRICE_INCLUDE_VAT, 0) + 1
		    ELSE 0
		END)
	  INTO L_TAX_FLG
	  FROM IAS_PARA_GEN;

	RETURN L_TAX_FLG;
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN 0;
    END CHK_USNG_TAX;

