						    END IF;
						END IF;

						/*BILL_ITM_CRSR.DIS_PER_DTL_DUMMY := BILL_ITM_CRSR.DIS_PER;

						IF NVL( G_USE_PRICE_INCLUDE_VAT, 0) = 0 THEN
						    BILL_ITM_CRSR.DIS_AMT_DTL_DUMMY := BILL_ITM_CRSR.DIS_AMT_DTL;
						ELSE
						    BILL_ITM_CRSR.DIS_AMT_DTL_DUMMY := BILL_ITM_CRSR.DIS_AMT_DTL_VAT;
						END IF;*/
					    END IF;

					ELSIF L_QT_PRM_METHOD = 3 THEN
					    IF L_APPRVD_FREEQTY_AS_DSCNT = 1 THEN

						IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
						    BILL_ITM_CRSR.DIS_AMT_DTL_VAT := (NVL( BILL_ITM_CRSR.I_PRICE_VAT, 0) / L_I_QTY) * NVL(L_QT_FREE_QTY, 0);
						    BILL_ITM_CRSR.DIS_PER := (NVL((NVL( BILL_ITM_CRSR.I_PRICE_VAT, 0) / L_I_QTY) * NVL(L_QT_FREE_QTY, 0), 0) / NVL( BILL_ITM_CRSR.I_PRICE_VAT, 0)) * 100;
						ELSE
						    BILL_ITM_CRSR.DIS_AMT_DTL := (NVL( BILL_ITM_CRSR.I_PRICE, 0) / L_I_QTY) * NVL(L_QT_FREE_QTY, 0);
						    BILL_ITM_CRSR.DIS_PER := (NVL((NVL( BILL_ITM_CRSR.I_PRICE, 0) / L_I_QTY) * NVL(L_QT_FREE_QTY, 0), 0) / NVL( BILL_ITM_CRSR.I_PRICE, 0)) * 100;
						END IF;

						IF G_ADD_DSCNT_ITM_QT_PRM_DSCNT = 1 THEN
						    GET_ITM_DSCNT_QT_PRM(P_DIS_PER => L_QT_DIS_PER, P_DIS_AMT => L_QT_DIS_AMT);

						    IF NVL(L_QT_DIS_AMT, 0) > 0 THEN
							IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
							    BILL_ITM_CRSR.DIS_AMT_DTL_VAT := NVL( BILL_ITM_CRSR.DIS_AMT_DTL_VAT, 0) + NVL(L_QT_DIS_AMT, 0);
							ELSE
							    BILL_ITM_CRSR.DIS_AMT_DTL := NVL( BILL_ITM_CRSR.DIS_AMT_DTL, 0) + NVL(L_QT_DIS_AMT, 0);
							END IF;

							BILL_ITM_CRSR.DIS_PER := (NVL( BILL_ITM_CRSR.DIS_AMT_DTL, 0) / NVL( BILL_ITM_CRSR.I_PRICE, 0)) * 100;
						    END IF;
						END IF;
					    ELSE
						BILL_ITM_CRSR.FREE_QTY := L_QT_FREE_QTY;
					    END IF;
					END IF;

					--CALC_DIS_VAT_AMT_RCRD;

				    END CLC_DISC_PRICE_ITM_PRM;

				ELSIF L_QT_PRM_TYPE IN (1,2) AND L_QT_PRM_METHOD IN (4,5) THEN --##FREE QTY FROM ANOTHER ITEM
				    GRNT_FREE_QTY_OTHR_ITM(P_MLTI_ITM_FLG => (CASE L_QT_PRM_METHOD WHEN 5 THEN 1 ELSE 0 END),P_ROWID => BILL_ITM_CRSR.ROWID,P_DOC_D_SEQ => BILL_ITM_CRSR.DOC_D_SEQ);
				END IF;

				UPDATE IAS_POS_BILL_DTL
				   SET I_PRICE = BILL_ITM_CRSR.I_PRICE,
				       I_PRICE_VAT = BILL_ITM_CRSR.I_PRICE_VAT,
				       VAT_AMT = BILL_ITM_CRSR.VAT_AMT,
				       DIS_PER = BILL_ITM_CRSR.DIS_PER,
				       DIS_AMT_DTL = BILL_ITM_CRSR.DIS_AMT_DTL,
				       DIS_AMT_DTL_VAT = BILL_ITM_CRSR.DIS_AMT_DTL_VAT,
				       DIS_AMT = NVL(BILL_ITM_CRSR.DIS_AMT_DTL, 0) + NVL(DIS_AMT_MST, 0),
				       FREE_QTY = BILL_ITM_CRSR.FREE_QTY,
				       QT_PRM_NO = L_QT_PRM_NO,
				       QT_PRM_SER = L_QT_PRM_SER,
				       QT_PRM_RCRD_NO = L_QT_PRM_RCRD_NO
				 WHERE ROWID = BILL_ITM_CRSR.ROWID;

				UPDATE IAS_POS_HUNG_BILLS
				   SET I_PRICE = BILL_ITM_CRSR.I_PRICE,
				       I_PRICE_VAT = BILL_ITM_CRSR.I_PRICE_VAT,
				       VAT_AMT = BILL_ITM_CRSR.VAT_AMT,
				       DIS_PER = BILL_ITM_CRSR.DIS_PER,
				       DIS_AMT_DTL = BILL_ITM_CRSR.DIS_AMT_DTL,
				       FREE_QTY = BILL_ITM_CRSR.FREE_QTY,
				       QT_PRM_NO = L_QT_PRM_NO,
				       QT_PRM_SER = L_QT_PRM_SER,
				       QT_PRM_RCRD_NO = L_QT_PRM_RCRD_NO,
				       QT_PRM_METHOD = L_QT_PRM_METHOD
				 WHERE BILL_SEQ = L_DOC_M_SQNC
				   AND DOC_D_SEQ = BILL_ITM_CRSR.DOC_D_SEQ
				   AND I_CODE = L_I_CODE
				   AND RCRD_NO = BILL_ITM_CRSR.RCRD_NO;

			    END START_CLC_PRM_FOR_ITEMS;

			END IF;

		    END LOOP;

		    L_ITM_PRM_APPLD := TRUE;
		ELSIF NOT L_GRP_PRM_APPLD AND PRM_CRSR.QT_PRM_METHOD IN (6,7) THEN--#GROUP PRM
		    DEBUG_PRC('23# START CALC GRP PRM');
		    IAS_CLC_QTN_PRM_GRP_PRC(P_DATE	     => G_BILL_DATE,
					    P_BILL_SER	     => P_DOC_SRL,
					    P_BILL_DOC_TYPE  => G_BILL_DOC_TYPE,
					    P_C_CODE	     => L_C_CODE,
					    P_C_GROUP_CODE   => L_C_GROUP_CODE,
					    P_C_CLASS	     => L_C_CLASS,
					    P_C_DEGREE	     => L_C_DEGREE,
					    P_DOC_M_SQNC     => L_DOC_M_SQNC,
					    P_C_CODE_CSH     => L_C_CODE_CSH,
					    P_CST_GRP_CSH    => L_CST_GRP_CSH,
					    P_BILL_AMT	     => L_BILL_AMT,
					    P_SYS_TYP	     => G_SYS_TYP,
					    P_NO_OF_DCML     => G_NO_OF_DECIMAL,
					    P_BILL_RATE      => G_BILL_RATE,
					    P_CALL_TYP	     => 2);

		    L_GRP_PRM_APPLD := TRUE;
		END IF;

	    ELSIF PRM_CRSR.QT_PRM_TYPE = 3 AND PRM_CRSR.QT_PRM_METHOD IN (1,2,3) AND PRM_CRSR.QT_PRM_ITM_TYPE IS NULL THEN

		G_DISC_AMT_MST := 0;
		G_DISC_AMT_MST_VAT := 0;
		L_DISC_AMT := 0;
		L_PRICE_LVL := G_PRICE_LVL;

		GET_QTN_PRM;

		DEBUG_PRC('9# G_TOT_ITM_PRICE='||G_TOT_ITM_PRICE||' <<>> L_W_CODE='||L_W_CODE||' <<>> L_QT_PRM_RCRD_NO='||L_QT_PRM_RCRD_NO||' <<>> L_QT_PRM_SER='||(CASE WHEN L_QT_PRM_SER IS NULL THEN 'QT_PRM_SER IS NULL' ELSE L_QT_PRM_SER||'' END)||' <<>> L_QT_PRM_METHOD='||L_QT_PRM_METHOD);

		IF L_QT_PRM_SER IS NOT NULL AND L_QT_PRM_RCRD_NO IS NOT NULL THEN

		    IF L_QT_PRM_METHOD = 1 AND NVL ( G_TOT_ITM_PRICE, 0) > 0 THEN
			IF L_QT_DIS_PER IS NOT NULL THEN
			    IF NVL ( G_USE_PRICE_INCLUDE_VAT, 0) = 1
			    THEN
				L_DISC_AMT_MST := ROUND (((NVL ( G_TOT_ITM_PRICE_VAT, 0) * NVL (L_QT_DIS_PER, 0)) / 100), G_NO_OF_DECIMAL);
			    ELSE
				L_DISC_AMT_MST := ROUND (((NVL ( G_TOT_ITM_PRICE, 0) * NVL (L_QT_DIS_PER, 0)) / 100), G_NO_OF_DECIMAL);
			    END IF;
			ELSE
			    L_DISC_AMT_MST := L_QT_DIS_AMT;
			END IF;

			IF NVL ( G_DISC_AMT_DTL, 0) > 0
			THEN
			    IF NVL ( G_DISC_AMT_DTL, 0) <= NVL (L_DISC_AMT_MST, 0) THEN
				L_DISC_AMT_MST := NVL (L_DISC_AMT_MST, 0) - NVL ( G_DISC_AMT_DTL, 0);
			    ELSE
				L_DISC_AMT_MST := 0;
			    END IF;
			END IF;

			IF NVL ( G_USE_PRICE_INCLUDE_VAT, 0) = 1
			THEN
			    G_DISC_AMT_MST_VAT := L_DISC_AMT_MST;

			    IF NVL ( G_TOT_ITM_PRICE_VAT, 0) > 0
			    THEN
				L_DISC_PRCNT_MST := (100 * NVL ( G_DISC_AMT_MST_VAT, 0)) / G_TOT_ITM_PRICE_VAT;
			    END IF;

			    G_DISC_AMT_MST := ROUND ((NVL ( G_TOT_ITM_PRICE, 0) * NVL ( L_DISC_PRCNT_MST, 0)) / 100, 10);
			ELSE
			    G_DISC_AMT_MST := L_DISC_AMT_MST;
			    L_DISC_PRCNT_MST := (100 * NVL ( G_DISC_AMT_MST, 0)) / G_TOT_ITM_PRICE;
			END IF;

			L_DISC_AMT_MST_PRM := G_DISC_AMT_MST;
			L_DISC_AMT := NVL ( G_DISC_AMT_MST, 0) + NVL ( G_DISC_AMT_DTL, 0);

			UPDATE IAS_POS_BILL_MST SET QT_PRM_NO = DECODE(PRM_CRSR.QT_PRM_METHOD,1,PRM_CRSR.QT_PRM_NO,QT_PRM_NO),
						    QT_PRM_SER = DECODE(PRM_CRSR.QT_PRM_METHOD,1,PRM_CRSR.QT_PRM_SER,QT_PRM_SER),
						    QT_PRM_RCRD_NO = DECODE(PRM_CRSR.QT_PRM_METHOD,1,L_QT_PRM_RCRD_NO,QT_PRM_RCRD_NO),
						    DISC_AMT_MST = DECODE(G_DISC_AMT_MST,0,DISC_AMT_MST,G_DISC_AMT_MST),
						    DISC_AMT_MST_VAT = DECODE(G_DISC_AMT_MST_VAT,0,DISC_AMT_MST_VAT,G_DISC_AMT_MST_VAT),
						    DISC_AMT = L_DISC_AMT
			 WHERE BILL_SRL = P_DOC_SRL;

		    ELSIF L_QT_PRM_METHOD IN (2,3) AND L_W_CODE IS NOT NULL THEN
			DEBUG_PRC('19# PRE INSERT ITEM (3,2)');
			GRNT_FREE_QTY_OTHR_ITM(P_MLTI_ITM_FLG => (CASE L_QT_PRM_METHOD WHEN 3 THEN 1 ELSE 0 END));
		    END IF;

		END IF;

	    ELSIF PRM_CRSR.QT_PRM_TYPE IN (4,5) THEN

		CLC_FREE_QTY_AS_DISC(P_DOC_SRL	       => P_DOC_SRL,
				     P_DATE	       => G_BILL_DATE,
				     P_BILL_DOC_TYPE   => L_BILL_DOC_TYPE,
				     P_CHK_QT_NO       => PRM_CRSR.QT_PRM_NO,
				     P_CHK_QT_SER      => PRM_CRSR.QT_PRM_SER,
				     P_DOC_M_SQNC      => L_DOC_M_SQNC,
				     P_SYS_TYP	       => G_SYS_TYP,
				     P_C_CODE	       => L_C_CODE,
				     P_C_GROUP_CODE    => L_C_GROUP_CODE,
				     P_C_CLASS	       => L_C_CLASS,
				     P_C_DEGREE        => L_C_DEGREE,
				     P_C_CODE_CSH      => L_C_CODE_CSH,
				     P_CST_GRP_CSH     => L_CST_GRP_CSH,
				     P_NO_OF_DCML      => G_NO_OF_DECIMAL,
				     P_CALL_TYP        => 2);

	    END IF;

	END LOOP;

	IF L_PRM_LOOP_INDX = 0 THEN
	    DEBUG_PRC('9999# NO QT_PRM EXIST');
	ELSE
	    <<UPDT_ITM_DISC>>
	    DECLARE
		L_DIS_PER NUMBER := 0;
		L_DIS_AMT NUMBER := 0;
	    BEGIN
		IF G_ADD_DSCNT_ITM_QT_PRM_DSCNT = 1 OR G_USE_PRICE_INCLUDE_VAT = 1 THEN
		    FOR ITM_CRSR IN (SELECT ROWID,DIS_AMT_DTL,DIS_AMT_DTL_VAT,I_PRICE,I_PRICE_VAT,W_CODE,I_QTY,I_CODE,ITM_UNT,BATCH_NO,EXPIRE_DATE,NVL(DIS_PER,0) DIS_PER,PRICE_LVL
				       FROM IAS_POS_BILL_DTL
				      WHERE BILL_SRL = P_DOC_SRL
					AND QT_PRM_SER IS NOT NULL
					AND NVL(FREE_QTY,0) = 0
					AND NVL(DIS_AMT_DTL,0) > 0
					AND NVL(DIS_AMT_DTL_VAT,0) = 0)
		    LOOP
			L_I_PRICE := ITM_CRSR.I_PRICE ;
			L_I_PRICE_VAT := ITM_CRSR.I_PRICE_VAT ;
			L_DIS_PER := ITM_CRSR.DIS_PER  ;
			L_W_CODE := NVL(ITM_CRSR.W_CODE,G_W_CODE);
			L_I_QTY := ITM_CRSR.I_QTY;
			L_I_CODE := ITM_CRSR.I_CODE;
			L_ITM_UNT := ITM_CRSR.ITM_UNT;
			L_PRICE_LVL := NVL(ITM_CRSR.PRICE_LVL,G_PRICE_LVL);
			L_BATCH_NO := ITM_CRSR.BATCH_NO;
			L_EXP_DATE := ITM_CRSR.EXPIRE_DATE;

			IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
			    ITM_CRSR.DIS_AMT_DTL_VAT := ITM_CRSR.DIS_AMT_DTL;
			ELSE
			    ITM_CRSR.DIS_AMT_DTL := ITM_CRSR.DIS_AMT_DTL;
			END IF;

			IF G_ADD_DSCNT_ITM_QT_PRM_DSCNT = 1 THEN
																																						       -- ADD ITEM DISCOUNT TO QTN PRM DISCOUNT
			    GET_ITM_DSCNT_QT_PRM(P_DIS_PER => L_DIS_PER, P_DIS_AMT => L_DIS_AMT);

			    IF NVL(L_DIS_AMT, 0) > 0 THEN

				IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
				    ITM_CRSR.DIS_AMT_DTL_VAT := NVL( ITM_CRSR.DIS_AMT_DTL_VAT, 0) + NVL(L_DIS_AMT, 0);
				ELSE
				    ITM_CRSR.DIS_AMT_DTL := NVL( ITM_CRSR.DIS_AMT_DTL, 0) + NVL(L_DIS_AMT, 0);
				END IF;

				ITM_CRSR.DIS_PER := (NVL( ITM_CRSR.DIS_AMT_DTL, 0) / NVL( ITM_CRSR.I_PRICE, 0)) * 100;
			    END IF;

			END IF;

			UPDATE IAS_POS_BILL_DTL
			   SET DIS_AMT_DTL = ITM_CRSR.DIS_AMT_DTL,
			       DIS_AMT_DTL_VAT = ITM_CRSR.DIS_AMT_DTL_VAT,
			       DIS_PER = ITM_CRSR.DIS_PER
			 WHERE ROWID = ITM_CRSR.ROWID;

		    END LOOP;
		END IF;
	    END UPDT_ITM_DISC;
	END IF;
    EXCEPTION
	WHEN OTHERS THEN
	    RAISE_APPLICATION_ERROR(-20999,'EXCEPTION IN CLC_DOC_QTN_PRM '||SQLERRM);
    END CLC_DOC_QTN_PRM;

    FUNCTION CHK_INVC_HAS_QT_PRM (P_PRM_TYP NUMBER DEFAULT 1,P_BILL_DOC_TYPE NUMBER,P_WHR VARCHAR2 DEFAULT NULL)--##1-GENERAL 2-CASH CUSTOMER 3-VOUCHER CUSTOMER
	RETURN BOOLEAN
    IS
	L_CNT NUMBER;
	L_DATE DATE := TO_DATE(TO_CHAR(SYSDATE,'DD/MM/YYY'),'DD/MM/YYY');
	L_DAY  VARCHAR2(4) := TO_CHAR(SYSDATE, 'D');
	L_TIME VARCHAR2(20) := TO_CHAR(SYSDATE, 'HH24:MI:SS');
    BEGIN

	EXECUTE IMMEDIATE 'SELECT 1
			     FROM IAS_QUT_PRM_MST
			    WHERE NVL(INACTIVE, 0) = 0
			      AND NVL(APPROVED, 0) = 1
			      AND CASE WHEN :P_BILL_DOC_TYPE IN (4,8) THEN 2 ELSE 1 END = CASE WHEN BILL_DOC_TYPE IS NULL THEN CASE WHEN :P_BILL_DOC_TYPE IN (4,8) THEN 2 ELSE 1 END ELSE BILL_DOC_TYPE END
			      AND :P_DATE BETWEEN F_DATE AND T_DATE
			      AND :P_DAY IN (FLD_DAY1, FLD_DAY2, FLD_DAY3, FLD_DAY4, FLD_DAY5, FLD_DAY6, FLD_DAY7)
			      AND NVL(USE_QTN_PRM_IN_RMS_SYS_FLG, 0) = 1
			      AND :P_TIME >= NVL(F_TIME, :P_TIME)
			      AND :P_TIME <= NVL(T_TIME, :P_TIME)'||
			      P_WHR||(CASE P_PRM_TYP WHEN 2 THEN ' AND IAS_QUT_PRM_MST.QT_PRM_CST_TYPE IN (5,7,8) ' WHEN 3 THEN ' AND IAS_QUT_PRM_MST.QT_PRM_CST_TYPE IN (1,2,3,4)' ELSE NULL END)||' AND ROWNUM <= 1'
		     INTO L_CNT
		     USING P_BILL_DOC_TYPE,
			   P_BILL_DOC_TYPE,
			   L_DATE,
			   L_DAY,
			   L_TIME,
			   L_TIME,
			   L_TIME,
			   L_TIME;

	RETURN TRUE;

    EXCEPTION
	WHEN OTHERS THEN
	    RETURN FALSE;
    END CHK_INVC_HAS_QT_PRM;
END GNR_QTN_PRM_PKG;
