			   P_ROOM_NO	   VARCHAR2,
			   P_YEAR_NO	   NUMBER,
			   P_WEB_SRVC_IP   VARCHAR2,
			   P_AD_TRMNL_NM   VARCHAR2 DEFAULT NULL,
			   P_DOC_TYP	   NUMBER DEFAULT 1)--##1-BILL 2- RETURN BILL
	RETURN VARCHAR2
    IS
	REQ		   UTL_HTTP.REQ;
	RESP		   UTL_HTTP.RESP;
	L_RSLT		   VARCHAR2(500);
	L_SRVC_NM	   VARCHAR2(100) := (CASE NVL(P_DOC_TYP,1) WHEN 2 THEN 'POS_RTRN_BILL' ELSE 'POS_BILL' END);
	L_BUFFER_SIZE	   NUMBER(10) := 512;
	L_LINE_SIZE	   NUMBER(10) := 50;
	L_LINES_COUNT	   NUMBER(10) := 20;
	L_STRING_REQUEST   VARCHAR2(512);
	L_LINE		   VARCHAR2(128);
	L_SUBSTRING_MSG    VARCHAR2(512);
	L_RAW_DATA	   RAW(1024);
	L_URL		   VARCHAR2(4000);
	L_SRVC_PATH	   VARCHAR2(4000) := NVL(P_WEB_SRVC_IP, 'localhost') || '/WebRestSys/Service1.svc/';
	L_METHOD	   VARCHAR2(20) := 'POST';
	NAME		   VARCHAR2(4000);
	BUFFER		   VARCHAR2(10000) := '';
	L_CONTENT	   VARCHAR2(32000);
	L_RES_NO	   NUMBER;
	L_CUR_NO	   NUMBER;

	FUNCTION GET_JSON_VAL(P_KEY VARCHAR2)
	    RETURN VARCHAR2
	IS
	    L_KEY_VAL	VARCHAR2(4000);
	BEGIN

	    SELECT VALUE
	      INTO L_KEY_VAL
	      FROM (WITH
			SRC AS(SELECT REPLACE(BUFFER, '{', '{,') IN_STR FROM DUAL),
			DATA (RES, STR, ORIG_STR, P)
			AS
			    (SELECT 'NULL' RES,
				    IN_STR STR,
				    IN_STR ORIG_STR,
				    1 P
			       FROM DUAL CROSS JOIN SRC
			     UNION ALL
			     SELECT REGEXP_SUBSTR(STR, '[^,]+') RES,
				    SUBSTR(STR, INSTR(STR, ',') + 1) STR,
				    ORIG_STR,
				    P + 1
			       FROM DATA
			      WHERE P <= REGEXP_COUNT(ORIG_STR, '[,]') + 1),
			COMPONENTS
			AS
			    (SELECT REGEXP_REPLACE(RES, '[{"}]') RES
			       FROM DATA
			      WHERE RES <> 'NULL')
		    SELECT REGEXP_SUBSTR(RES,
					 '[^:]+',
					 1,
					 1)
			       KEY,
			   REGEXP_SUBSTR(RES,
					 '[^:]+',
					 1,
					 2)
			       VALUE
		      FROM COMPONENTS)
	     WHERE INSTR(UPPER(KEY) , P_KEY)  >  0 ;

	    RETURN L_KEY_VAL;
	EXCEPTION
	    WHEN NO_DATA_FOUND THEN
		RETURN NULL;
	    WHEN OTHERS THEN
		RETURN NULL;
	END GET_JSON_VAL;

	PROCEDURE CALL_WEB_SRVC
	IS
	BEGIN
	    REQ := UTL_HTTP.BEGIN_REQUEST(L_URL, L_METHOD, ' HTTP/1.1');

	    IF L_METHOD = 'POST' THEN
		UTL_HTTP.SET_HEADER(REQ, 'content-type', 'application/json;charset=UTF-8');

		L_CONTENT := CONVERT(L_CONTENT, 'AL32UTF8');
		UTL_HTTP.SET_BODY_CHARSET('UTF-8');

		UTL_HTTP.SET_HEADER(R => REQ, NAME => 'Content-Length', VALUE => LENGTHB(L_CONTENT));

	       <<REQUEST_LOOP>>

		FOR I IN 0 .. CEIL(LENGTH(L_CONTENT) / L_BUFFER_SIZE) - 1
		LOOP
		    L_SUBSTRING_MSG := SUBSTR(L_CONTENT, I * L_BUFFER_SIZE + 1, L_BUFFER_SIZE);

		    BEGIN
			L_RAW_DATA := UTL_RAW.CAST_TO_RAW(L_CONTENT);
			UTL_HTTP.WRITE_RAW(R => REQ, DATA => L_RAW_DATA);
		    EXCEPTION
			WHEN NO_DATA_FOUND THEN
			    EXIT REQUEST_LOOP;
		    END;

		END LOOP REQUEST_LOOP;

	    END IF;

	    RESP := UTL_HTTP.GET_RESPONSE(REQ);

	    BEGIN

		IF RESP.STATUS_CODE = 200 THEN
		    UTL_HTTP.READ_LINE(RESP, BUFFER);

		    IF GET_JSON_VAL(P_KEY => '_ERRNO') = '0' THEN
			L_RSLT := 0;
		    ELSE
			L_RSLT := GET_JSON_VAL(P_KEY => '_ERRMSG');
		    END IF;
		ELSE
		    L_RSLT := 'ERROR IN REQUEST ,Response Code : ' || RESP.STATUS_CODE;
		END IF;

		UTL_HTTP.END_RESPONSE(RESP);
	    EXCEPTION
		WHEN UTL_HTTP.END_OF_BODY THEN
		    UTL_HTTP.END_RESPONSE(RESP);
		    L_RSLT := '-3 : ' || SQLERRM;
	    END;

	END CALL_WEB_SRVC;

	PROCEDURE GET_RES_NO
	IS
	BEGIN
	    L_URL := L_SRVC_PATH || 'GetOccupiedRoomsNo?Room_no=' || P_ROOM_NO;
	    L_METHOD := 'GET';

	    CALL_WEB_SRVC;

	    L_RES_NO := GET_JSON_VAL(P_KEY => 'RES_NO');
	END GET_RES_NO;
    BEGIN

	/*BEGIN
