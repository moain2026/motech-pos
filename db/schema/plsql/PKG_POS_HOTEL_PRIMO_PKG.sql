	GET_RES_NO;

	L_URL := L_SRVC_PATH || (CASE NVL(P_DOC_TYP,1) WHEN 2 THEN 'AddReturnService' ELSE 'AddServiceCharg' END);
	L_METHOD := 'POST';

	L_CONTENT :=
	    '{
		"Amount": "' || ROUND(NVL(P_BILL_AMT, 0),15) || '",
		"Service_No": "4",
		"Amount_Type":"1",
		"Curr_Change": "' || ROUND(P_BILL_RATE,15) || '",
		"Curr_Code": "' || P_A_CY || '",
		"Res_No": "' || L_RES_NO || '",
		"Room_No": "' || P_ROOM_NO || '",
		"S_Date": "' || TO_CHAR(SYSDATE, 'yyyy-MM-dd') || '",
		"Service_Name": "'||L_SRVC_NM||'",
		"RestBranch_No":"' || P_BRN_NO || '",
		"Hotel_No":"1",
		"Brn_No":"' || P_BRN_NO ||'",
		"Year_No":"' || P_YEAR_NO || '",
		"Ad_Trmnl_Nm":"' || P_AD_TRMNL_NM || '",
		"Aserial": "' || (CASE NVL(P_DOC_TYP,1) WHEN 2 THEN P_RT_BILL_NO ELSE P_BILL_NO END) || '",
		' || (CASE NVL(P_DOC_TYP,1) WHEN 2 THEN '"BillNo": "' ||P_BILL_NO|| '",' ELSE NULL END)||'
		"Source":"Onyx POS System"
	     }';

	CALL_WEB_SRVC;

	RETURN L_RSLT;
    EXCEPTION
	WHEN OTHERS THEN
	    UTL_HTTP.END_RESPONSE(RESP);
	    RETURN '-4 : ' || SQLERRM;
    END ADD_SRVC_CHRG;

    FUNCTION GET_OCCPD_ROOMS(P_WEB_SRVC_URL VARCHAR2)
	RETURN CLOB
    IS
	L_REQ		    UTL_HTTP.REQ;
	L_RES		    UTL_HTTP.RESP;
	L_CONTENT_IN_RAW    RAW(32767);
	L_CURRENT_CHARS     INTEGER;
	L_CHARS_PER_CHUNK   NUMBER;
	L_CLOB_OFFSET	    NUMBER;
	L_CONTENT_LENGTH    NUMBER;
	L_CLOB_LENGTH	    NUMBER;
	L_CHUNK_STRING	    VARCHAR2(32767);
	L_RES_TEXT	    CLOB;
	L_HTTP_RES_CODE     NUMBER;
	L_SRVR_WEB_SRVC     VARCHAR2(100);
	L_APPLICATIONID     VARCHAR2(500);
	L_SENDERID	    NUMBER;
	L_URL		    VARCHAR2(4000);
	BUFFER		    VARCHAR2(4000);

	L_CONTENT	    VARCHAR2(4000);
    BEGIN

	IF P_WEB_SRVC_URL IS NULL THEN
	    BEGIN
		SELECT HTL_SYS_WEB_SRVC_IP
		  INTO L_URL
		  FROM IAS_PARA_POS;
	    EXCEPTION
		WHEN OTHERS THEN
		    L_URL := 'localhost';
	    END;
	END IF;

	L_URL := NVL(NVL(P_WEB_SRVC_URL,L_URL), 'localhost') || '/WebRestSys/Service1.svc//GetOccupiedRooms';
	L_CONTENT := '';

	IF L_URL IS NULL OR L_URL = NULL THEN
	    RAISE_APPLICATION_ERROR(-20001, 'URL can not be empty');
	END IF;

	L_REQ := UTL_HTTP.BEGIN_REQUEST(L_URL, 'GET', UTL_HTTP.HTTP_VERSION_1_1);
	UTL_HTTP.SET_HEADER(L_REQ, 'content-type', 'application/json; charset=utf-8');

	UTL_HTTP.SET_HEADER(L_REQ, 'user-agent', 'mozilla/4.0');
	UTL_HTTP.SET_BODY_CHARSET(L_REQ, 'UTF-8');
	UTL_HTTP.SET_HEADER(L_REQ, 'Transfer-Encoding', 'chunked');

	L_CLOB_LENGTH := DBMS_LOB.GETLENGTH(L_CONTENT);
	L_CHARS_PER_CHUNK := FLOOR(32767 / 4);

	L_CLOB_OFFSET := 1;
	L_CONTENT_LENGTH := 0;
	L_CLOB_OFFSET := 1;

	BEGIN
	    WHILE L_CLOB_OFFSET <= L_CLOB_LENGTH
	    LOOP
		L_CURRENT_CHARS := L_CHARS_PER_CHUNK;
		DBMS_LOB.READ(L_CONTENT,
			      L_CURRENT_CHARS,
			      L_CLOB_OFFSET,
			      L_CHUNK_STRING);
		EXIT WHEN L_CURRENT_CHARS < 1;

		UTL_HTTP.WRITE_TEXT(L_REQ, L_CHUNK_STRING);
		L_CLOB_OFFSET := L_CLOB_OFFSET + L_CURRENT_CHARS;
	    END LOOP;
	EXCEPTION
	    WHEN NO_DATA_FOUND THEN
		NULL;
	END;

	L_RES := UTL_HTTP.GET_RESPONSE(L_REQ);

	UTL_HTTP.READ_TEXT(L_RES, L_RES_TEXT);
	L_HTTP_RES_CODE := L_RES.STATUS_CODE;
	UTL_HTTP.END_RESPONSE(L_RES);

	IF L_HTTP_RES_CODE <> 200 THEN
	    RETURN L_RES_TEXT;
	    --RETURN NULL;
	ELSE
	    RETURN L_RES_TEXT;
	END IF;
    EXCEPTION
	WHEN UTL_HTTP.END_OF_BODY THEN
	    UTL_HTTP.END_REQUEST(L_REQ);
	    UTL_HTTP.END_RESPONSE(L_RES);
	    --RAISE;
	WHEN OTHERS THEN
	    UTL_HTTP.END_REQUEST(L_REQ);
	    UTL_HTTP.END_RESPONSE(L_RES);
	    RAISE;
    END GET_OCCPD_ROOMS;
END POS_HOTEL_PRIMO_PKG;
