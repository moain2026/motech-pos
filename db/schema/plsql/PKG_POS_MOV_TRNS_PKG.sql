  PROCEDURE UPD_CPN_CRD_AMT_PRC (P_POS_SCMA IN VARCHAR2,P_DB_LNK IN VARCHAR2,P_CPN_CRD_NO In Varchar2 DEFAULT NULL) IS
   V_USE_PREV_PAID_CARD  NUMBER;
   V_USE_CARD_MORE_ONE	 NUMBER;
  BEGIN
    BEGIN
     SELECT USE_PREV_PAID_CARD,USE_CARD_MORE_ONE
       INTO V_USE_PREV_PAID_CARD,V_USE_CARD_MORE_ONE
	FROM IAS_PARA_POS;
    EXCEPTION WHEN OTHERS THEN
      V_USE_PREV_PAID_CARD:=0;
      V_USE_CARD_MORE_ONE :=0;
    END;

    IF NVL(V_USE_PREV_PAID_CARD,0)=1 THEN
	EXECUTE IMMEDIATE ' DELETE FROM IAS_POS_CUST_CARD_AMT_TMP ';
      IF P_CPN_CRD_NO IS NULL THEN
	      EXECUTE IMMEDIATE ' INSERT INTO IAS_POS_CUST_CARD_AMT_TMP
	      SELECT CARD_NO FROM
	     (SELECT CARD_NO,REM_CARD_AMT,PROCESSED FROM IAS_POS_CUSTOMER_CARD_AMOUNT
		    MINUS
	      SELECT CARD_NO,REM_CARD_AMT,PROCESSED FROM '||P_POS_SCMA||'.IAS_POS_CUSTOMER_CARD_AMOUNT@'||P_DB_LNK||')';
      ELSE
	    BEGIN
	      INSERT INTO IAS_POS_CUST_CARD_AMT_TMP VALUES(P_CPN_CRD_NO);
	    EXCEPTION WHEN OTHERS THEN
	      RAISE_APPLICATION_ERROR(-2000,'Error  When Insert into card tmp table ' || '' || SQLERRM);
	    END;
      END IF;
      IF NVL(V_USE_CARD_MORE_ONE,0) =1 THEN
	DECLARE
	 CURSOR C_CARD IS SELECT CARD_NO,REM_CARD_AMT
	  FROM IAS_POS_CUSTOMER_CARD_AMOUNT
	    WHERE  EXISTS(SELECT 1
			    FROM  IAS_POS_CUST_CARD_AMT_TMP
			     WHERE  IAS_POS_CUST_CARD_AMT_TMP.CARD_NO=IAS_POS_CUSTOMER_CARD_AMOUNT.CARD_NO
			      AND ROWNUM <= 1  )
	    ORDER BY CARD_NO;
	  V_REM_AMT_SRV NUMBER;
	     BEGIN
	       FOR I IN C_CARD LOOP
		   BEGIN
		      V_REM_AMT_SRV :=IAS_GEN_PKG.GET_CNT ('SELECT REM_CARD_AMT from
				  '||P_POS_SCMA||'.IAS_POS_CUSTOMER_CARD_AMOUNT@'||P_DB_LNK||'	WHERE CARD_NO =  '||I.CARD_NO);
		   EXCEPTION WHEN OTHERS THEN
		      V_REM_AMT_SRV:=0 ;
		   END;
		   IF NVL(I.REM_CARD_AMT,0)< NVL(V_REM_AMT_SRV,0) THEN
			 BEGIN
				EXECUTE IMMEDIATE ' Update '||P_POS_SCMA||'.IAS_POS_CUSTOMER_CARD_AMOUNT@'||P_DB_LNK||'
						  SET PROCESSED    = 1 ,
						      REM_CARD_AMT = '||NVL(I.REM_CARD_AMT,0)||'
						     WHERE CARD_NO = '||I.CARD_NO ;
			 EXCEPTION WHEN OTHERS THEN
			   NULL ;
			 END;
		   END IF;
	       END LOOP  ;
	     END;
      ELSE
	 IF P_CPN_CRD_NO IS NULL THEN
	     BEGIN
		EXECUTE IMMEDIATE ' Update '||P_POS_SCMA||'.IAS_POS_CUSTOMER_CARD_AMOUNT@'||P_DB_LNK||' a
				  SET PROCESSED   = 1 ,
				      REM_CARD_AMT= 0
				   WHERE   EXISTS(SELECT 1
				     FROM  IAS_POS_CUST_CARD_AMT_TMP
				       WHERE  IAS_POS_CUST_CARD_AMT_TMP.CARD_NO=A.CARD_NO
					AND ROWNUM  <= 1  )';
	     EXCEPTION WHEN OTHERS THEN
	       NULL ;
	     END;
	 ELSE
	     BEGIN
		EXECUTE IMMEDIATE ' Update '||P_POS_SCMA||'.IAS_POS_CUSTOMER_CARD_AMOUNT@'||P_DB_LNK||'
				  SET PROCESSED    = 1 ,
				      REM_CARD_AMT = 0
				   WHERE  CARD_NO= '||P_CPN_CRD_NO||'';
	     EXCEPTION WHEN OTHERS THEN
	       NULL ;
	     END;
	 END IF;
      END IF;
    END IF;
  EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20002,'ERROR WHEN UPDATE CPN CRD AMT ' || '' || SQLERRM);
 END UPD_CPN_CRD_AMT_PRC;		
