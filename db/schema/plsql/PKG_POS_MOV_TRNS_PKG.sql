 FUNCTION  GET_FLDS_TBL_FNC (P_TBL_NM  IN VARCHAR2,P_OWNR IN VARCHAR2,P_EXPT_CLMN IN VARCHAR2 DEFAULT NULL )RETURN VARCHAR2 IS
   V_ALL_COL   VARCHAR2(5000);
 BEGIN
 IF P_EXPT_CLMN IS NOT NULL THEN
  SELECT listagg(COLUMN_NAME,',') WITHIN GROUP (ORDER BY COLUMN_NAME) INTO V_ALL_COL
       FROM ALL_TAB_COLS WHERE	COLUMN_NAME NOT LIKE 'SYS%' AND COLUMN_NAME NOT LIKE '%$%' AND OWNER='P_OWNR' AND TABLE_NAME = 'P_TBL_NM'
       AND COLUMN_NAME NOT IN ('P_EXPT_CLMN');
 ELSE
     SELECT listagg(COLUMN_NAME,',') WITHIN GROUP (ORDER BY COLUMN_NAME) INTO V_ALL_COL
       FROM ALL_TAB_COLS WHERE	COLUMN_NAME NOT LIKE 'SYS%' AND COLUMN_NAME NOT LIKE '%$%' AND OWNER='P_OWNR' AND TABLE_NAME = 'P_TBL_NM';
 END IF;
     RETURN  V_ALL_COL;
 EXCEPTION WHEN OTHERS THEN
    RETURN  NULL;
 END GET_FLDS_TBL_FNC;
