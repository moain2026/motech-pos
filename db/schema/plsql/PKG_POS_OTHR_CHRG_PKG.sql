						  SC_NM,
						  SC_PRCNT,
						  SC_AMT,
						  A_CY,
						  AC_RATE,
						  RCRD_NO,
						  DOC_NO,
						  DOC_SRL,
						  SC_A_CY,
						  SC_AC_RATE,
						  VAT_AMT,
						  VAT_PRCNT,
						  AD_U_ID,
						  AD_DATE,
						  AD_TRMNL_NM)
					   VALUES(P_DOC_TYP,
						  OTHR_CHRG_C.SC_NO,
						  L_SC_NM,
						  OTHR_CHRG_C.SC_PRCNT,
						  L_SC_AMT,	    --SC_AMT,
						  G_A_CY,
						  G_BILL_RATE,	    --AC_RATE,
						  V_RCRD_NO,
						  G_BILL_NO,
						  G_BILL_SRL,
						  OTHR_CHRG_C.SC_A_CY,	 --SC_A_CY,
						  OTHR_CHRG_C.SC_AC_RATE,--SC_AC_RATE,
						  OTHR_CHRG_C.VAT_AMT,
						  OTHR_CHRG_C.VAT_PRCNT,
						  G_AD_U_ID,
						  G_AD_DATE,
						  G_AD_TRMNL_NM);
		    EXCEPTION
			WHEN OTHERS THEN
			    RAISE_APPLICATION_ERROR(-20202,'EXCEPTION  WHEN POS_RQ_OTHR_CHRG_MVMNT  '||GET_SQLERRM);
		    END INSRT_RQ_OTHR_CHRG_MVMNT_PRC;


		BEGIN
		    IF OTHR_CHRG_C.SC_NO IS NULL THEN
			RETURN	IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 21086)||CHR(32)||IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 125)
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349)||' = '||G_BILL_NO
				||CHR(10)||GET_SQLERRM;
		    END IF;

		    IF OTHR_CHRG_C.SC_PRCNT IS NULL THEN
			RETURN	IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 920)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 2297)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 21086)||CHR(32)||IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 125)
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||' = '||OTHR_CHRG_C.SC_NO
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349) ||' = '||G_BILL_NO
				||CHR(10)||GET_SQLERRM;
		    END IF;

		    IF	OTHR_CHRG_C.A_CY IS NULL THEN
			RETURN	IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 9)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 21086)||CHR(32)||IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 125)
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||' = '||OTHR_CHRG_C.SC_NO
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349) ||' = '||G_BILL_NO
				||CHR(10)||GET_SQLERRM;
		    ELSE
			BEGIN
			    SELECT 1
			      INTO V_CNT
			      FROM EX_RATE
			     WHERE CUR_CODE = OTHR_CHRG_C.A_CY AND ROWNUM <=1;
			EXCEPTION
			    WHEN OTHERS THEN
				V_CNT := 0;
			END;

			IF NVL (V_CNT, 0) = 0 THEN
			    RETURN  IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 9)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 21086)||' = '||OTHR_CHRG_C.A_CY||CHR(32)||IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 125)
				    ||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||' = '||OTHR_CHRG_C.SC_NO
				    ||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349) ||' = '||G_BILL_NO
				    ||CHR(10)||GET_SQLERRM;
