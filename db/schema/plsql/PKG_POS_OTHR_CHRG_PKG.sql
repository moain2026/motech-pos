				      ,EXTRACTVALUE (VALUE (XMLMSTDMY), '*/RCRD_NO    ') AS  RCRD_NO
				      ,EXTRACTVALUE (VALUE (XMLMSTDMY), '*/VAT_AMT    ') AS  VAT_AMT
				      ,EXTRACTVALUE (VALUE (XMLMSTDMY), '*/VAT_PRCNT  ') AS  VAT_PRCNT
			  FROM TABLE  (XMLSEQUENCE (EXTRACT (L_XML_INPT, '/'||V_XML_TAG||'/POS_OTHR_CHRG_MVMNT'))) XMLMSTDMY)
	    LOOP
		DECLARE
		    PROCEDURE INSRT_OTHR_CHRG_MVMNT_PRC
		    IS
		    BEGIN

			INSERT INTO POS_OTHR_CHRG_MVMNT(DOC_TYPE,
