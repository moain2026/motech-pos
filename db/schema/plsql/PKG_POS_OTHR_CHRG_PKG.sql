	     WHERE ORDER_NO = G_BILL_NO AND ORDER_SER = G_BILL_SRL AND ROWNUM <= 1;
	EXCEPTION
	    WHEN OTHERS THEN
		RETURN 'EXCEPTION WHEN GET Sales_Order DATA ORDER_SER ='||P_BILL_NO||CHR(13)||GET_SQLERRM;
