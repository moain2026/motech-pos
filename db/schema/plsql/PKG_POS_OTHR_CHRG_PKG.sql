			RETURN	IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 524)
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 2760) ||' = '||OTHR_CHRG_C.SC_AMT
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||' = '||OTHR_CHRG_C.SC_NO
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349) ||' = '||G_BILL_NO
				||CHR(10)||GET_SQLERRM;
		    END IF;

		    L_SC_AMT := OTHR_CHRG_C.SC_AMT;

		    IF OTHR_CHRG_C.SC_A_CY IS NOT NULL AND OTHR_CHRG_C.SC_AC_RATE IS NULL THEN
			OTHR_CHRG_C.SC_AC_RATE := IAS_USR_PKG.GET_CUR_RATE(P_ACY=>OTHR_CHRG_C.SC_A_CY,P_USR_NO => G_AD_U_ID);
		    END IF;

		    V_RCRD_NO := OTHR_CHRG_C.RCRD_NO;
		    IF V_RCRD_NO IS NULL THEN
			BEGIN
			    SELECT NVL(MAX(RCRD_NO),0)+1
				   INTO V_RCRD_NO
				   FROM POS_OTHR_CHRG_MVMNT
				   WHERE DOC_TYPE=P_DOC_TYP AND BILL_NO = G_BILL_NO AND BILL_SRL =G_BILL_SRL;
			EXCEPTION
			    WHEN OTHERS THEN
				V_RCRD_NO := 1;
			END;
		    END IF;

		    BEGIN
			SELECT 1,SC_NAME,AMT_TYPE,SC_TYPE
			  INTO V_CNT,L_SC_NM,L_AMT_TYPE,L_SC_TYPE
			  FROM SALES_CHARGES
			 WHERE --NVL(FILL_AUTO, 0) = 1
			       NVL(INACTIVE, 0) = 0
			   AND NVL(INV_ITEM, 0) = 0
			   AND (NVL(USE_IN_POS_FLG,0) = 1 Or NVL(Use_Sc_In_Sho_Sys_Flg,0)=1 )
			   AND NVL(SC_TYPE,0) IN(1,2)
			   AND AMT_TYPE IN (0,1)
			   AND DECODE(BILL_DOC_TYPE,NULL,0,BILL_DOC_TYPE) = DECODE(BILL_DOC_TYPE,NULL,0,G_BILL_TYPE)
			   AND SC_NO = OTHR_CHRG_C.SC_NO
			   And nvl(Sc_Add_Frc_Invoice,0)<>1
			   AND ROWNUM <=1;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_CNT := 0;
		    END;
		    IF V_CNT = 0 THEN
			RETURN	IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 5707)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 876)||CHR(32)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 2297)
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 1318)||' = '||OTHR_CHRG_C.SC_NO
				||CHR(10)||IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => 349) ||' = '||G_BILL_NO
				||CHR(10)||GET_SQLERRM;
		    END IF;

		    IF L_SC_TYPE = 2 THEN
			L_SC_AMT := L_SC_AMT * -1;	
		    END IF;
		    If P_DOC_TYP=4 Then
				    BEGIN
					INSRT_OTHR_CHRG_MVMNT_PRC;
				    EXCEPTION
					WHEN OTHERS THEN
					    RETURN 'EXCEPTION WHEN INSRT_OTHR_CHRG_MVMNT '||GET_SQLERRM;
				    END;
			       Elsif P_DOC_TYP=149 Then
				    BEGIN
					INSRT_RQ_OTHR_CHRG_MVMNT_PRC;
				    EXCEPTION
					WHEN OTHERS THEN
					    RETURN 'EXCEPTION  WHEN INSRT_OTHR_CHRG_MVMNT '||GET_SQLERRM;
				    END;
			       End If;
		END;

	    END LOOP;
	ELSE
	    FOR SAL_CHRG_C IN (SELECT CHRG.SC_NO,
				     CHRG.SC_NAME,
				     NVL (CHRG.SC_TYPE, 0)     SC_TYPE,
				     CHRG.AMT_TYPE AMT_TYPE,
				     NVL (CHRG.USE_VAT, 0)     USE_VAT,
				     CHRG.CLC_VAT_OTHR_TYP,
				     CHRG.ALLOW_RETURN,
				     CHRG.BILL_DOC_TYPE,
				     CHRG.SC_CST_TYPE,
				     CHRG.SC_WCODE_TYPE,
				     CHRG.A_CODE,
				     CHRG.AC_CODE_DTL,
				     CHRG.AC_DTL_TYP,
				     CHRG.A_CY,
				     CHRG.AC_RATE,
				     NVL (CHRG.AMT, 0)	       AMT
				FROM SALES_CHARGES CHRG
			       WHERE	-- NVL (CHRG.FILL_AUTO, 0) = 1
				      NVL (CHRG.DIFF_CMPNS_QTY_SR, 0) = 0
				     AND NVL (CHRG.INACTIVE, 0) = 0
				     AND NVL (CHRG.INV_ITEM, 0) = 0
				     AND (NVL(USE_IN_POS_FLG,0) = 1 Or NVL(Use_Sc_In_Sho_Sys_Flg,0)=1 )
				     AND NVL (CHRG.SC_TYPE,0) IN(1,2)
				     AND AMT_TYPE IN (0,1)
				     And nvl(Sc_Add_Frc_Invoice,0)<>1
				     AND DECODE(CHRG.BILL_DOC_TYPE,NULL,0,CHRG.BILL_DOC_TYPE) = DECODE(CHRG.BILL_DOC_TYPE,NULL,0,G_BILL_TYPE)
			    ORDER BY SC_NO)
	    LOOP

		DECLARE
		    PROCEDURE INSRT_OTHR_CHRG_MVMNT_PRC
		    IS
		    BEGIN
			INSERT INTO POS_OTHR_CHRG_MVMNT(DOC_TYPE,
						  SC_NO,
						  SC_NM,
						  SC_PRCNT,
						  SC_AMT,
						  A_CY,
						  AC_RATE,
						  RCRD_NO,
						  BILL_NO,
						  BILL_SRL,
						  SC_A_CY,
						  SC_AC_RATE,
						  VAT_AMT,
						  VAT_PRCNT,
						  AD_U_ID,
