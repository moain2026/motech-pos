						  AD_TRMNL_NM)
					   VALUES(P_DOC_TYP,
						  OTHR_CHRG_C.SC_NO,
						  L_SC_NM,
						  OTHR_CHRG_C.SC_PRCNT,
						  L_SC_AMT,	    --SC_AMT,
						  G_A_CY,
						  G_BILL_RATE,	    --AC_RATE,
						  V_RCRD_NO,
						  G_BILL_NO,
						  G_BILL_SRL,
						  OTHR_CHRG_C.SC_A_CY,	 --SC_A_CY,
						  OTHR_CHRG_C.SC_AC_RATE,--SC_AC_RATE,
						  OTHR_CHRG_C.VAT_AMT,
						  OTHR_CHRG_C.VAT_PRCNT,
						  G_AD_U_ID,
