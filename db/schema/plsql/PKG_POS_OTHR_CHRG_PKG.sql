	End If;
	IF P_XML_INPT IS NOT NULL THEN
	    L_XML_INPT	:= XMLTYPE.CREATEXML(P_XML_INPT);
	    FOR OTHR_CHRG_C IN (SELECT	 EXTRACTVALUE (VALUE (XMLMSTDMY), '*/SC_NO	') AS  SC_NO
				      ,EXTRACTVALUE (VALUE (XMLMSTDMY), '*/SC_PRCNT   ') AS  SC_PRCNT
				      ,EXTRACTVALUE (VALUE (XMLMSTDMY), '*/SC_AMT     ') AS  SC_AMT
