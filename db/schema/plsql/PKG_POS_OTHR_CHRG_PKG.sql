		    PROCEDURE CALC_VAT_AMT_OTHR (P_SRVC_AMT NUMBER)
		    IS
		    BEGIN

			IF NVL (YS_TAX_PKG.GET_CLC_TAX_TYP ( G_CLC_TYP_NO_TAX), 0) = 0
			THEN
			    IF G_USE_VAT = 1 AND TO_DATE ( G_BILL_DATE, 'DD/MM/YYYY') >= TO_DATE (G_ACTIVE_VAT_DATE, 'DD/MM/YYYY')
			    THEN
				IF SAL_CHRG_C.SC_NO IS NOT NULL
				THEN
				    BEGIN
					L_VAT_PRCNT := YS_TAX_PKG.GET_INPT_PRCNT(P_CLC_TYP_NO => G_CLC_TYP_NO_TAX, P_INPT_TYP => 1, P_INPT_CODE => SAL_CHRG_C.SC_NO);
				    EXCEPTION
					WHEN OTHERS
					THEN
					    NULL;
				    END;

				    IF NVL (P_SRVC_AMT, 0) > 0 AND L_VAT_PRCNT IS NOT NULL
				    THEN
					L_VAT_AMT := P_SRVC_AMT * L_VAT_PRCNT / 100;
				    ELSE
					L_VAT_PRCNT := NULL;
					L_VAT_AMT := NULL;
				    END IF;
				END IF;
			    END IF;
			ELSE
			    L_VAT_PRCNT := 0;
			    L_VAT_AMT := NULL;
			END IF;
		    END CALC_VAT_AMT_OTHR;

		BEGIN
		    IF SAL_CHRG_C.AMT_TYPE = 1 THEN -- 0-PRCNT 1-AMT
			IF NVL(G_BILL_AMT,0) > 0 THEN
			    IF G_USE_PRICE_INCLUDE_VAT = 1 THEN
				L_SC_AMT := ((NVL(SAL_CHRG_C.AMT,0) * 100) / (NVL(YS_TAX_PKG.GET_INPT_PRCNT (G_CLC_TYP_NO_TAX,1,SAL_CHRG_C.SC_NO),0) + 100));
			    ELSE
				L_SC_AMT := NVL(SAL_CHRG_C.AMT,0);
			    END IF;  	 	
			ELSE
			   L_SC_AMT := 0;
			END IF;
		    ELSE
			L_SC_AMT := NVL(G_BILL_AMT,0) * NVL(SAL_CHRG_C.AMT,0)/100;
		    END IF;

		    IF SAL_CHRG_C.SC_TYPE = 2 THEN
			L_SC_AMT := L_SC_AMT * -1;	
		    END IF;

		    IF SAL_CHRG_C.SC_NO IS NOT NULL AND L_SC_AMT <> 0 AND G_USE_VAT = 1
		    THEN
			V_RCRD_NO := V_RCRD_NO + 1;
			CALC_VAT_AMT_OTHR (L_SC_AMT);
		    ELSE
			L_VAT_PRCNT := 0;
			L_VAT_AMT := NULL;
		    END IF;
		    IF P_DOC_TYP=4 THEN
				    BEGIN
					INSRT_OTHR_CHRG_MVMNT_PRC;
				    EXCEPTION
					WHEN OTHERS THEN
					    RETURN 'EXCEPTION WHEN INSRT_OTHR_CHRG_MVMNT_PRC '||GET_SQLERRM;
				    END;
			 ELSIF P_DOC_TYP=149 THEN
					  BEGIN
					INSRT_RQ_OTHR_CHRG_MVMNT_PRC;
				    EXCEPTION
					WHEN OTHERS THEN
					    RETURN 'EXCEPTION WHEN INSRT_OTHR_CHRG_MVMNT_PRC '||GET_SQLERRM;
				    END;
			 END IF;
		END;
	    END LOOP;
	END IF;

	IF P_DOC_TYP=4 Then
	  V_TBL_MST_NM	 :='IAS_POS_BILL_MST';
	  V_TBL_DTL_NM	 :='IAS_POS_BILL_DTL';
	  V_P_FLD_DOC_SER:='BILL_SRL';
	  V_FLD_DOC_AMT  :='BILL_AMT';
	  V_FLD_DOC_SER_INPT :='M.BILL_SRL';
	  V_TBL_INPT_NM  :='POS_OTHR_CHRG_MVMNT';
	  BEGIN
	    SELECT NVL(SUM(SC_AMT), 0), NVL(SUM(VAT_AMT), 0)
	      INTO L_OTHR_AMT, L_VAT_AMT_OTHR
	      FROM POS_OTHR_CHRG_MVMNT
	     WHERE DOC_TYPE=P_DOC_TYP AND BILL_SRL = G_BILL_SRL;
	  EXCEPTION   WHEN OTHERS THEN
		L_OTHR_AMT     := 0;
		L_VAT_AMT_OTHR := 0;
	  END;
	Elsif P_DOC_TYP=149 Then
	  V_TBL_MST_NM	 :='SALES_ORDER';
	  V_TBL_DTL_NM	 :='ORDER_DETAIL';
	  V_P_FLD_DOC_SER:='ORDER_NO';
	  V_FLD_DOC_AMT  :='ORDER_AMT';
	  V_FLD_DOC_SER_INPT :='M.DOC_SRL';
	  V_TBL_INPT_NM  :='POS_RQ_OTHR_CHRG_MVMNT';
	  BEGIN
	    SELECT NVL(SUM(SC_AMT), 0), NVL(SUM(VAT_AMT), 0)
	      INTO L_OTHR_AMT, L_VAT_AMT_OTHR
	      FROM POS_RQ_OTHR_CHRG_MVMNT
	     WHERE DOC_TYPE=P_DOC_TYP AND DOC_SRL=G_BILL_SRL;
	  EXCEPTION WHEN OTHERS THEN
		L_OTHR_AMT     := 0;
		L_VAT_AMT_OTHR := 0;
	  END;
	End If;
	IF L_OTHR_AMT > 0 THEN
	    YS_TAX_PKG.CLC_INPT_TAX_AFTR_SAVE( P_CLC_TYP_NO	    => G_CLC_TYP_NO_TAX,
					       P_DOC_TYP	    => P_DOC_TYP, -- Doc_Type In  Ias_Sys.Ias_Docjv_Type_Systems
					       P_DOC_NO 	    => G_BILL_NO,
					       P_DOC_SER	    => G_BILL_SRL,
					       P_BILL_DOC_TYP	    => G_BILL_TYPE,
					       P_DOC_JV_TYP	    => G_SI_TYPE,
					       P_DOC_DATE	    => G_BILL_DATE,
					       P_A_CY		    => G_A_CY,
					       P_AC_RATE	    => G_BILL_RATE,
					       P_TBL_INPT_NM	    => V_TBL_INPT_NM,
					       P_TBL_MST_NM	    => V_TBL_MST_NM,
					       P_TBL_DTL_NM	    => V_TBL_DTL_NM,
					       P_FLD_DOC_SER	    => V_P_FLD_DOC_SER,
					       P_FLD_DOC_AMT	    => V_FLD_DOC_AMT,
					       P_FLD_DOC_SER_INPT   => V_FLD_DOC_SER_INPT,
					       P_FLD_TAX_A_CODE     => 'TD.AC_CODE_AR',
					       P_INPT_TYP	    => 1,
					       P_FLD_INPT_CODE	    => 'M.SC_NO',
					       P_FLD_INPT_AMT	    => 'M.SC_AMT',
					       P_FLD_DOC_SEQ	    => NULL,
					       P_CLC_DOC_TYP	    => 1, -- CLC_DOC_TYP  1= SALES ,2 = PURCHASE ,
					       P_DR_CR_FLG	    => -1,
					       P_LNG_NO 	    => 1);
	END IF;

	IF P_DOC_TYP=4 THEN
			UPDATE IAS_POS_BILL_MST
			   SET OTHR_AMT = NVL(L_OTHR_AMT, 0),
			       VAT_AMT_OTHR = NVL(L_VAT_AMT_OTHR, 0)
			 WHERE BILL_SRL = G_BILL_SRL;
		
			UPDATE IAS_POS_BILL_DTL
			   SET OTHR_AMT = DECODE(NVL(I_QTY,0)*NVL(I_PRICE,0),0,0,(NVL (L_OTHR_AMT, 0) / (G_BILL_AMT)) * (NVL(I_PRICE,0))),
			       VAT_AMT_OTHR = DECODE(NVL(I_QTY,0)*NVL(I_PRICE,0),0,0,(NVL (L_VAT_AMT_OTHR, 0) / (G_BILL_AMT)) * (NVL(I_PRICE,0)))
			 WHERE BILL_SRL = G_BILL_SRL;
       ELSIF P_DOC_TYP=149 THEN
		  BEGIN
		   UPDATE SALES_ORDER
		   SET OTHR_AMT = NVL(L_OTHR_AMT, 0),
		       VAT_AMT_OTHR =NVL(L_VAT_AMT_OTHR,0)
		    WHERE ORDER_SER  =G_BILL_SRL;
	
		   UPDATE ORDER_DETAIL
		    SET OTHR_AMT = DECODE(NVL(I_QTY,0)*NVL(I_PRICE,0),0,0,(NVL (L_OTHR_AMT, 0) / (G_BILL_AMT)) * (NVL(I_PRICE,0))),
			VAT_AMT_OTHR =DECODE(NVL(I_QTY,0)*NVL(I_PRICE,0),0,0,(NVL (L_VAT_AMT_OTHR, 0) / (G_BILL_AMT)) * (NVL(I_PRICE,0)))
			WHERE ORDER_SER = G_BILL_SRL;
		 EXCEPTION WHEN OTHERS THEN
		    NULL;
		 END;
       END IF;
	RETURN NULL;
  EXCEPTION
	WHEN OTHERS THEN
	    RETURN 'EXCEPTION IN INSRT_OTHR_CHRG_MVMNT	'||GET_SQLERRM;
  END INSRT_OTHR_CHRG_MVMNT;
