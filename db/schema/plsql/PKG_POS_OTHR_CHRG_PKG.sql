    BEGIN
	RETURN REPLACE(L_SQLERRM,'"','');
    END GET_SQLERRM;
--#----------------------------------------------------------------------------------------------------------#--
    FUNCTION LOAD_PRMTRS RETURN VARCHAR2
    IS
    BEGIN
	BEGIN
	    SELECT NVL(CLC_TYP_NO_TAX, 0)
	      INTO G_CLC_TYP_NO_TAX
	      FROM IAS_POS_MACHINE
	     WHERE MACHINE_NO = G_MACHINE_NO AND ROWNUM <= 1;
	EXCEPTION
	    WHEN OTHERS THEN
		RETURN 'EXCEPTION WHEN GET CLC_TYP_NO_TAX IN MACHINE_NO '||G_MACHINE_NO ||CHR(13)||GET_SQLERRM;
	END;

	BEGIN
	    SELECT NVL(USE_PRICE_INCLUDE_VAT, 0),
		   ACTIVE_VAT_DATE,
		   NVL(IAS_PARA_AR.CALC_VAT_AMT_TYPE, 0),
		   NVL(USE_VAT, 0),
		   NVL(IAS_PARA_AR.NO_OF_DECIMAL_AR, 2)
	      INTO G_USE_PRICE_INCLUDE_VAT,
		   G_ACTIVE_VAT_DATE,
		   G_CALC_VAT_AMT_TYPE,
		   G_USE_VAT,
		   G_NO_OF_DECIMAL
	      FROM IAS_PARA_GEN, IAS_PARA_AR
	      WHERE ROWNUM<=1;
	EXCEPTION
	    WHEN OTHERS THEN
		RETURN 'ERROR WHEN GET PARAMETERS FROM IAS_PARA_AR IAS_PARA_GEN ' || GET_SQLERRM;
	END ;

	RETURN NULL;
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN 'ERROR IN GET PARAMETERS';
    END LOAD_PRMTRS;
