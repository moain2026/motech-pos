		    END;
		    G_DOC_SRL := G_BRN_YEAR||LPAD(G_BRN_NO,6,'0')||LPAD(G_SRVR_NO,3,0)||170||LPAD(G_TRMNL_NO,5,0)||G_DOC_NO;
	   ELSE
	       IF G_DOC_NO IS NULL THEN
		   RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_NO');
	       END IF;
	       IF G_DOC_SRL IS NULL THEN
		   RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_SRL');
	       END IF;
	       BEGIN
		    SELECT COUNT(1)
		      INTO V_CNT
		    FROM POS_GNR_RCPTS
		      WHERE DOC_SRL = G_DOC_SRL  AND ROWNUM <=1;
		EXCEPTION WHEN OTHERS THEN
		    V_CNT:=0;
		END;
		IF NVL(V_CNT,0)>0 THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 8,P_MSG_TXT => 'DOC_SRL='||G_DOC_SRL);
		END IF;
	    END IF;

	    IF MST_CRSR.PYMNT_TYPE = 4 THEN
		IF MST_CRSR.CR_CARD_TYPE IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CR_CARD_TYPE');
		END IF;

		--# check if CR_CARD_TYPE is there
		BEGIN
		    SELECT  1 INTO V_CNT
			      FROM IAS_CR_CARD_TYPES
			     WHERE CR_CARD_TYPE = MST_CRSR.CR_CARD_TYPE
			       AND ROWNUM <=1 ;
		EXCEPTION
		    WHEN OTHERS THEN
			V_CNT := 0;
		END;

		IF V_CNT = 0 THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 4847,P_MSG_TXT => 'CR_CARD_TYPE');
		END IF;

		IF MST_CRSR.CR_CARD_NO IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CR_CARD_NO');
		END IF;

		--# check if CR_CARD_NO is there
		BEGIN
		    SELECT 1 INTO V_CNT
			     FROM CREDIT_CARD_TYPES
			    WHERE MACHINE_NO   = G_TRMNL_NO
			      AND CR_CARD_TYPE = MST_CRSR.CR_CARD_TYPE
			      AND CR_CARD_NO   = MST_CRSR.CR_CARD_NO
			      AND ROWNUM <=1;
		EXCEPTION
		    WHEN OTHERS THEN
			V_CNT := 0;
		END;

		IF V_CNT = 0 THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 1488,P_MSG_TXT => 'CR_CARD_NO');
		END IF;

