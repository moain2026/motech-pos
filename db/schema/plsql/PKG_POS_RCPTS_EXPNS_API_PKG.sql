		BEGIN
		    SELECT 1 INTO V_CNT
			     FROM CASH_AT_BANK
			    WHERE BANK_NO = MST_CRSR.BNK_NO;

		EXCEPTION
		    WHEN OTHERS THEN
			V_CNT := 0;
		END;

		IF V_CNT = 0 THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 600,P_MSG_TXT => 'BNK_NO');
		END IF;

		--# check if privilage of bank
		BEGIN
		    SELECT 1 INTO V_CNT
			     FROM CASH_AT_BANK
			    WHERE EXISTS( SELECT 1
					    FROM PRIV_CASH
					   WHERE CASH_AT_BANK.BANK_NO = PRIV_CASH.CASH_NO
					     AND CASH_TYPE = 2
					     AND U_ID = G_USR_NO
					     AND A_CY = MST_CRSR.CUR_CODE
					     AND NVL (ADD_FLAG, 0) = 1
					     AND ROWNUM <= 1)
			      AND BANK_NO =MST_CRSR.BNK_NO;

		EXCEPTION
		    WHEN OTHERS THEN
			V_CNT := 0;
		END;

		IF V_CNT = 0 THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 6794,P_MSG_TXT => MST_CRSR.BNK_NO);
		END IF;

		IF MST_CRSR.CHQ_NO IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CHQ_NO');
		END IF;
	    END IF;

	    IF MST_CRSR.DOC_NO_REF IS NOT NULL THEN
		IF MST_CRSR.DOC_SRL_REF IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_SRL_REF');
		END IF;

		/*IF MST_CRSR.DOC_TYP_REF IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_TYP_REF');
		END IF;*/
	    END IF;

	    IF MST_CRSR.DOC_SRL_REF IS NOT NULL THEN
		IF MST_CRSR.DOC_NO_REF IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_NO_REF');
		END IF;

	       /* IF MST_CRSR.DOC_TYP_REF IS NULL THEN
		    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_TYP_REF');
		END IF;*/
	    END IF;

	    IF MST_CRSR.DOC_SRL_REF IS NOT NULL AND NVL(MST_CRSR.DOC_AMT,0) > 0 THEN
		--# GET ORDR_AMT
		BEGIN
		    SELECT NVL(ORDER_AMT,0)
		      INTO V_ORDR_AMT
		      FROM SALES_ORDER
		     WHERE ORDER_SER = MST_CRSR.DOC_SRL_REF
		       AND ROWNUM <= 1;

		EXCEPTION
		  WHEN OTHERS THEN
		     V_ORDR_AMT := 0;
		END;
		IF  NVL(MST_CRSR.DOC_AMT,0) >  NVL(V_ORDR_AMT,0) THEN
		    V_ERR_TXT :=  'MSG_NO:' || 296||CHR (13) || ' - ' || IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => 296)
								 || ' ' || IAS_GEN_PKG.GET_PROMPT (G_LNG_NO, 1217)
								 || '='
								 || (NVL(MST_CRSR.DOC_AMT,0))
								 || CHR (13)
								 || IAS_GEN_PKG.GET_PROMPT (G_LNG_NO, 1818)
								 || '='
								 || V_ORDR_AMT;
		    RETURN '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"' || REPLACE(V_ERR_TXT,'"','') || '","_ErrNo":-8 } }';

		END IF;


	    END IF;

	    --##CHK AC_CODE_DTL
	    IF MST_CRSR.AC_CODE_DTL IS NOT NULL THEN
		IF NVL(MST_CRSR.AC_DTL_TYP,0) = 3 THEN
		    BEGIN
			SELECT 1 INTO V_CNT
				 FROM CUSTOMER,ACCOUNT
				WHERE ACCOUNT.A_CODE =CUSTOMER.C_A_CODE
				  AND NVL (INACTIVE, 0)  =0
				  AND CUSTOMER.C_A_CODE  =MST_CRSR.A_CODE
				  AND CUSTOMER.C_CODE	 =MST_CRSR.AC_CODE_DTL
				  AND ACCOUNT.AC_DTL_TYP =MST_CRSR.AC_DTL_TYP
				  AND ACCOUNT.AC_DTL_TYP =3
				  AND ROWNUM <=1;
		    EXCEPTION
			WHEN OTHERS THEN
			    V_CNT := 0;
		    END;
		    IF V_CNT = 0 THEN
			Begin
			SELECT 1 INTO V_CNT FROM IAS_CST_ACCNT
			WHERE C_CODE  =MST_CRSR.AC_CODE_DTL
			AND AC_TYP=3
			AND ROWNUM <=1;
			EXCEPTION WHEN OTHERS THEN
			    V_CNT := 0;
			END;
			IF NVL(V_CNT,0) = 0 THEN
			  RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 3,P_MSG_TXT => '');
			END IF;
		    END IF;
		END IF;
	    ELSIF MST_CRSR.AC_CODE_DTL IS NULL AND NVL(MST_CRSR.AC_DTL_TYP,0)<>0 THEN
		   RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 4559,P_MSG_TXT => '');
	    END IF;

	    --##INSERT DATA
	    BEGIN
		INSERT INTO POS_GNR_RCPTS( DOC_NO
					  ,DOC_SRL
					  ,DOC_TYPE
					  ,DOC_DATE
					  ,DOC_TIME
					  ,REF_NO
					  ,CUR_CODE
					  ,EX_RATE
					  ,DOC_AMT
					  ,DOC_DSC
					  ,CASH_NO
					  ,MACHINE_NO
					  ,EXTRNL_PST
					  ,PYMNT_TYPE
					  ,A_CODE
					  ,AC_CODE_DTL
					  ,AC_DTL_TYP
					  ,CR_CARD_TYPE
					  ,CR_CARD_NO
					  ,CR_CARD_COMM_PER
					  ,CR_CARD_DSC
					  ,CR_CARD_NO_REF
					  ,MOBILE_NO
					  ,CUST_CODE
					  ,CC_CODE
					  ,PJ_NO
					  ,ACTV_NO
					  ,CHQ_NO
					  ,BNK_NO
					  ,PST_FLG
					  ,PRCSS_FLG
					  ,AD_U_ID
					  ,AD_DATE
					  ,AD_TRMNL_NM
					  ,DOC_NO_REF
					  ,DOC_SRL_REF
					  ,DOC_TYP_REF
					  ,SHFT_SRL
					  ,BRN_NO
					  ,BRN_YEAR
					  ,BRN_USR
					  ,CMP_NO)
		       VALUES (G_DOC_NO
			      ,G_DOC_SRL
			      ,MST_CRSR.DOC_TYPE
			      ,L_DOC_DATE
			      ,MST_CRSR.DOC_TIME
			      ,MST_CRSR.REF_NO
			      ,MST_CRSR.CUR_CODE
			      ,MST_CRSR.EX_RATE
			      ,NVL(MST_CRSR.DOC_AMT,0)
			      ,MST_CRSR.DOC_DSC
			      ,G_CASH_NO
			      ,G_TRMNL_NO
			      ,NVL(MST_CRSR.EXTRNL_PST,0)
			      ,MST_CRSR.PYMNT_TYPE
			      ,MST_CRSR.A_CODE
			      ,MST_CRSR.AC_CODE_DTL
			      ,MST_CRSR.AC_DTL_TYP
			      ,MST_CRSR.CR_CARD_TYPE
			      ,MST_CRSR.CR_CARD_NO
			      ,MST_CRSR.CR_CARD_COMM_PER
			      ,MST_CRSR.CR_CARD_DSC
			      ,MST_CRSR.CR_CARD_NO_REF
			      ,MST_CRSR.MOBILE_NO
			      ,MST_CRSR.CUST_CODE
			      ,MST_CRSR.CC_CODE
			      ,MST_CRSR.PJ_NO
			      ,MST_CRSR.ACTV_NO
			      ,MST_CRSR.CHQ_NO
			      ,MST_CRSR.BNK_NO
			      ,NVL(MST_CRSR.PRCSS_FLG,0)
			      ,0
			      ,G_USR_NO
			      ,G_AD_DATE
			      ,MST_CRSR.AD_TRMNL_NM
			      ,MST_CRSR.DOC_NO_REF
			      ,MST_CRSR.DOC_SRL_REF
			      ,MST_CRSR.DOC_TYP_REF
			      ,MST_CRSR.SHFT_SRL
			      ,G_BRN_NO
			      ,G_BRN_YEAR
			      ,G_BRN_USR
			      ,G_CMP_NO);

			      L_ROW_INSRTD := TRUE;
	    EXCEPTION
		WHEN OTHERS THEN
		    ROLLBACK;
		    RETURN '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"ERROR WHEN INSERT INTO POS_GNR_RCPTS ' || GET_SQLERRM || '","_ErrNo":-9 } }';
	    END;


	END LOOP;

	IF G_DOC_NO IS NOT NULL AND G_DOC_SRL IS NOT NULL THEN

	    IF L_ROW_INSRTD THEN
		L_ERR_MSG := PST_COMMIT_PRC;

		IF L_ERR_MSG IS NOT NULL THEN
		    L_JSON_RSLT := '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"' || REPLACE(L_ERR_MSG,'"','') || '","_ErrNo":-10 } }';
		END IF;
	    ELSE
		L_JSON_RSLT := '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"NO DATA INSERTED","_ErrNo":-11 } }';
	    END IF;


	ELSE
	    L_JSON_RSLT := '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"NO DATA INSERTED","_ErrNo":-12 } }';
	END IF;

	IF L_JSON_RSLT IS NULL THEN
	    COMMIT;
	    L_JSON_RSLT := '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"SUCCESSFUL","_ErrNo":0 } }';
	ELSE
	    ROLLBACK;
	END IF;

	RETURN L_JSON_RSLT;

    EXCEPTION
	WHEN OTHERS THEN
	    ROLLBACK;
	    RETURN '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"ERROR IN INSRT_RCPTS_CNTS ' || GET_SQLERRM || '","_ErrNo":-13 } }';
    END INSRT_RCPTS;
--#----------------------------------------------------------------------------------------------------------#--
 FUNCTION INSRT_EXPNS(P_XML_INPT IN CLOB,P_ONLINE_FLG NUMBER) RETURN VARCHAR2
    IS
	L_XML_INPT	 XMLTYPE := XMLTYPE.CREATEXML(P_XML_INPT);
	L_ERR_MSG	 VARCHAR2(2000);
	L_JSON_RSLT	 VARCHAR2(2000);
	L_ROW_INSRTD BOOLEAN := FALSE;
	L_DOC_DATE	 POS_GNR_EXPNS.DOC_DATE%TYPE;
	V_CNT		 NUMBER;
	V_PRVS_AMT	 NUMBER;
	V_ORDR_AMT	 NUMBER;
	V_ERR_TXT	 VARCHAR2(2000);

	FUNCTION GET_ERR_FNC(P_LBL_NO NUMBER DEFAULT NULL,P_MSG_NO NUMBER  DEFAULT NULL,P_MSG_TXT VARCHAR2  DEFAULT NULL)
	    RETURN VARCHAR2
	IS
	    L_ERR_TXT VARCHAR2(2000) := NULL;
	BEGIN
	    IF P_MSG_NO IS NOT NULL AND P_LBL_NO IS NOT NULL THEN
		L_ERR_TXT := 'MSG_NO:' || P_MSG_NO || ' - ' || IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => P_MSG_NO) || ' - ' || IAS_GEN_PKG.GET_PROMPT(P_LNG_NO => G_LNG_NO, P_LBL_NO => P_LBL_NO) || ' ' || P_MSG_TXT;
	    ELSIF P_MSG_NO IS NOT NULL THEN
		L_ERR_TXT :=  'MSG_NO:' || P_MSG_NO || ' - ' || IAS_GEN_PKG.GET_MSG(P_LNG_NO => G_LNG_NO, P_MSG_NO => P_MSG_NO) || ' ' || P_MSG_TXT;
	    END IF;

	    ROLLBACK;
	    RETURN '{"_Result": { "_Doc_No":"' || G_DOC_NO || '","_Doc_Srl":"' || G_DOC_SRL || '","_ErrMsg":"' || REPLACE(L_ERR_TXT,'"','') || '","_ErrNo":-70 } }';

	END GET_ERR_FNC;
    BEGIN

	FOR MST_CRSR IN (SELECT EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_NO') AS DOC_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_SRL') AS DOC_SRL,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_DATE') AS DOC_DATE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_TIME') AS DOC_TIME,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/REF_NO') AS REF_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/CUR_CODE') AS CUR_CODE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/EX_RATE') AS EX_RATE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_AMT') AS DOC_AMT,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_DSC') AS DOC_DSC,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/CASH_NO') AS CASH_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/MACHINE_NO') AS MACHINE_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/EXTRNL_PST') AS EXTRNL_PST,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/A_CODE') AS A_CODE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/AC_CODE_DTL') AS AC_CODE_DTL,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/AC_DTL_TYP') AS AC_DTL_TYP,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/MOBILE_NO') AS MOBILE_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/CC_CODE') AS CC_CODE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/PJ_NO') AS PJ_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/ACTV_NO') AS ACTV_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/AD_U_ID') AS AD_U_ID,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/AD_DATE') AS AD_DATE,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/AD_TRMNL_NM') AS AD_TRMNL_NM,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_NO_REF') AS DOC_NO_REF,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_SRL_REF') AS DOC_SRL_REF,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/DOC_TYP_REF') AS DOC_TYP_REF,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/SHFT_SRL') AS SHFT_SRL,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/BRN_NO') AS BRN_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/BRN_YEAR') AS BRN_YEAR,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/BRN_USR') AS BRN_USR,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/CMP_NO') AS CMP_NO,
				EXTRACTVALUE (VALUE (XMLMSTDMY), '*/LNG_NO') AS LNG_NO
			   FROM TABLE(XMLSEQUENCE(EXTRACT(L_XML_INPT, '/POS_EXPNS/POS_GNR_EXPNS'))) XMLMSTDMY)
	LOOP
	    G_DOC_NO := MST_CRSR.DOC_NO;
	    G_DOC_SRL := MST_CRSR.DOC_SRL;
