		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'BRN_YEAR');
	    END IF;

	    IF G_USR_NO IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'AD_U_ID');
	    END IF;

	    IF MST_CRSR.PYMNT_TYPE IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'PYMNT_TYPE');
	    END IF;
	    IF MST_CRSR.DOC_TYPE IS NULL OR MST_CRSR.DOC_TYPE NOT IN (1,2) THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 6599,P_MSG_TXT => 'DOC_TYPE');
	    END IF;

	    IF MST_CRSR.CUR_CODE IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CUR_CODE');
	    END IF;

	    IF NVL(MST_CRSR.DOC_AMT,0) = 0 THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 506,P_MSG_TXT => 'DOC_AMT');
	    END IF;

