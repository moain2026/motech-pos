		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'EX_RATE');
	    END IF;

	    IF G_TRMNL_NO IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => 4540,P_MSG_NO => 644,P_MSG_TXT => '');
	    END IF;
	    IF	NVL(G_USE_WRK_SHFT,0)>1  AND MST_CRSR.SHFT_SRL IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => 11633,P_MSG_NO => 644,P_MSG_TXT => 'SHFT_SRL');
	    END IF;

	    IF G_CASH_NO IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CASH_NO');
	    END IF;


	    G_SRVR_NO :=POS_GNR_PKG.GET_SRVR_NO_FNC;
	    L_DOC_DATE := TO_CHAR(SYSDATE,'DD/MM/YYYY');

	   IF NVL(P_ONLINE_FLG,0)>0 THEN
		    --##GENERATE DOC_NO AND DOC_SRL
		    BEGIN
