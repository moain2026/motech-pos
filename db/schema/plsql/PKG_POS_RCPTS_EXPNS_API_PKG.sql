       V_ERR_MSG := LOAD_PRMTRS;
	IF V_ERR_MSG IS NOT NULL THEN
	    RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 670,P_MSG_TXT =>V_ERR_MSG);
	END IF;

	    G_DOC_NO := MST_CRSR.DOC_NO;
	    G_DOC_SRL := MST_CRSR.DOC_SRL;
	    G_LNG_NO := NVL(MST_CRSR.LNG_NO,1);
	    G_TRMNL_NO := MST_CRSR.MACHINE_NO;
	    G_CMP_NO := MST_CRSR.CMP_NO;
	    G_BRN_NO := MST_CRSR.BRN_NO;
	    G_BRN_YEAR := TO_CHAR(YS_GEN_PKG.GET_CURDATE, 'YYYY');
	    G_BRN_USR := MST_CRSR.BRN_USR;
	    G_USR_NO := MST_CRSR.AD_U_ID;
	    G_CASH_NO:= MST_CRSR.CASH_NO;
	    G_AD_DATE := YS_GEN_PKG.GET_CURDATE;

	    G_LBL_NO := NULL;
	    G_MSG_NO := NULL;
	    G_MSG_TXT := NULL;

	    IF	MST_CRSR.DOC_DATE IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_DATE');
	    END IF;
	    IF	MST_CRSR.DOC_TIME IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'DOC_TIME');
	    END IF;
	    IF G_BRN_NO IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'BRN_NO');
	    END IF;

	    IF G_CMP_NO IS NULL THEN
		RETURN GET_ERR_FNC(P_LBL_NO => NULL,P_MSG_NO => 644,P_MSG_TXT => 'CMP_NO');
	    END IF;

	    IF G_BRN_USR IS NULL THEN
