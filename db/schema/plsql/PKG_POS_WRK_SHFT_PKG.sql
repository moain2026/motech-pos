	    L_JSON_RSLT := '{"_Result": { "_DOC_NO":"' || G_SHFT_NO || '","_DOC_SRL":"' || G_SHFT_SRL || '","_ErrMsg":"NO DATA INSERTED","_ErrNo":-11 } }';
	END IF;

	IF L_JSON_RSLT IS NULL THEN
