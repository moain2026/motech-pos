	ELSE
	    ROLLBACK;
	END IF;

	RETURN L_JSON_RSLT;

    EXCEPTION
	WHEN OTHERS THEN
	    ROLLBACK;
	    RETURN '{"_Result": { "_DOC_NO":"' || G_SHFT_NO || '","_DOC_SRL":"' || G_SHFT_SRL || '","_ErrMsg":"ERROR IN INSRT_WRK_SHFTS ' || GET_SQLERRM || '","_ErrNo":-70 } }';
