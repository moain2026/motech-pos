	RETURN VARCHAR2
    IS
	V_CUR_CODE_STNDR   EX_RATE.CUR_CODE_STNDR%TYPE;
    BEGIN

	SELECT CUR_CODE_STNDR
	  INTO V_CUR_CODE_STNDR
	  FROM EX_RATE
	 WHERE CUR_CODE = P_CUR_CODE;

	RETURN (V_CUR_CODE_STNDR);
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN (NULL);
    END GET_CUR_CODE_STNDR;

