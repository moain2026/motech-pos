	RETURN NUMBER
    IS
	V_EX_RATE   EX_RATE.CUR_RATE%TYPE;
    BEGIN

	SELECT CUR_RATE
	  INTO V_EX_RATE
	  FROM EX_RATE
	 WHERE (   CUR_CODE = P_CUR_CODE
		OR CUR_CODE_STNDR = P_CUR_CODE)
	   AND ROWNUM <= 1;

	RETURN V_EX_RATE;
    EXCEPTION
	WHEN OTHERS THEN
	    RETURN (NULL);
    END GET_CUR_EXRATE;

